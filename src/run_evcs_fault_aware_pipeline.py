def _raw_window_tensor_for_meta(df: pd.DataFrame, meta: pd.DataFrame, config: Config) -> Tuple[np.ndarray, List[str]]:
    raw_cols = [c for cols in FEATURE_COLUMNS_BY_GROUP.values() for c in cols]
    arr = df[raw_cols].astype(float).to_numpy()
    W = int(config.window_steps)
    tensors = []
    for end_step in meta["window_end_step"].astype(int).values:
        end = end_step + 1
        start = max(0, end - W)
        win = arr[start:end, :]
        if len(win) < W:
            pad = np.repeat(win[[0], :], W - len(win), axis=0) if len(win) else np.zeros((W, len(raw_cols)))
            win = np.vstack([pad, win])
        tensors.append(win)
    return np.stack(tensors, axis=0), raw_cols


def _standardize_sequence_tensors(Xtr: np.ndarray, Xte: np.ndarray) -> Tuple[np.ndarray, np.ndarray]:
    mu = np.nanmean(Xtr.reshape(-1, Xtr.shape[-1]), axis=0)
    sig = np.nanstd(Xtr.reshape(-1, Xtr.shape[-1]), axis=0) + 1e-9
    Xtr_s = np.nan_to_num((Xtr - mu) / sig, nan=0.0, posinf=0.0, neginf=0.0)
    Xte_s = np.nan_to_num((Xte - mu) / sig, nan=0.0, posinf=0.0, neginf=0.0)
    return Xtr_s.astype(np.float32), Xte_s.astype(np.float32)


def _rocket_style_transform(X: np.ndarray, config: Config, n_kernels: Optional[int] = None) -> Tuple[np.ndarray, Dict[str, Any]]:
    """Small dependency-free ROCKET-style random convolution transform.

    This is not a formal MiniROCKET implementation; it is included as a strong,
    reproducible random-convolution time-series baseline when sktime is not installed.
    """
    rng = np.random.default_rng(config.random_seed + 3601)
    n, W, C = X.shape
    K = int(n_kernels or config.timeseries_random_kernel_count)
    lengths = rng.choice([3, 5, 7], size=K)
    channels = rng.integers(0, C, size=K)
    kernels = []
    for L in lengths:
        k = rng.normal(0, 1, size=int(L)).astype(np.float32)
        k = k - k.mean()
        k = k / (np.linalg.norm(k) + 1e-9)
        kernels.append(k)
    feats = np.zeros((n, 2 * K), dtype=np.float32)
    for i in range(n):
        for j, (L, ch, k) in enumerate(zip(lengths, channels, kernels)):
            sig = X[i, :, int(ch)]
            conv = np.convolve(sig, k, mode="valid") if len(sig) >= L else np.array([0.0])
            feats[i, 2 * j] = float(np.max(conv))
            feats[i, 2 * j + 1] = float(np.mean(conv > 0.0))
    meta = {"n_kernels": K, "features": 2 * K}
    return feats, meta


def _train_torch_sequence_classifier(model_type: str, Xtr: np.ndarray, ytr: np.ndarray, Xte: np.ndarray, yte: np.ndarray, config: Config) -> Tuple[Optional[np.ndarray], str, float, float]:
    try:
        import torch
        import torch.nn as nn
        from torch.utils.data import DataLoader, TensorDataset
    except Exception as exc:
        return None, f"skipped: PyTorch unavailable ({type(exc).__name__})", np.nan, np.nan

    try:
        seed = int(config.random_seed + (371 if model_type == "1D-CNN" else 379))
        torch.manual_seed(seed)
        device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
        # Optional cap to keep runtime manageable.
        if len(Xtr) > config.max_deep_train_windows:
            rng = np.random.default_rng(seed)
            idx = rng.choice(len(Xtr), size=int(config.max_deep_train_windows), replace=False)
            Xtr2, ytr2 = Xtr[idx], ytr[idx]
        else:
            Xtr2, ytr2 = Xtr, ytr
        n_classes = len(CLASS_NAMES)
        C = Xtr2.shape[2]

        class CNN1D(nn.Module):
            def __init__(self):
                super().__init__()
                self.net = nn.Sequential(
                    nn.Conv1d(C, 48, kernel_size=3, padding=1), nn.ReLU(),
                    nn.Conv1d(48, 64, kernel_size=3, padding=2, dilation=2), nn.ReLU(),
                    nn.AdaptiveAvgPool1d(1), nn.Flatten(), nn.Dropout(0.10), nn.Linear(64, n_classes)
                )
            def forward(self, x):
                return self.net(x.transpose(1, 2))

        class GRUClassifier(nn.Module):
            def __init__(self):
                super().__init__()
                self.gru = nn.GRU(input_size=C, hidden_size=48, batch_first=True, num_layers=1)
                self.fc = nn.Linear(48, n_classes)
            def forward(self, x):
                out, h = self.gru(x)
                return self.fc(h[-1])

        net = CNN1D() if model_type == "1D-CNN" else GRUClassifier()
        net.to(device)
        counts = np.bincount(ytr2.astype(int), minlength=n_classes).astype(float)
        weights = counts.sum() / np.maximum(counts, 1.0)
        weights = weights / weights.mean()
        loss_fn = nn.CrossEntropyLoss(weight=torch.tensor(weights, dtype=torch.float32, device=device))
        opt = torch.optim.Adam(net.parameters(), lr=1e-3, weight_decay=1e-4)
        ds = TensorDataset(torch.tensor(Xtr2, dtype=torch.float32), torch.tensor(ytr2.astype(int), dtype=torch.long))
        loader = DataLoader(ds, batch_size=128, shuffle=True)
        t0 = time.perf_counter()
        net.train()
        for _ in range(int(config.timeseries_deep_epochs)):
            for xb, yb in loader:
                xb, yb = xb.to(device), yb.to(device)
                opt.zero_grad()
                loss = loss_fn(net(xb), yb)
                loss.backward()
                opt.step()
        train_time = time.perf_counter() - t0
        net.eval()
        t1 = time.perf_counter()
        with torch.no_grad():
            logits = []
            for i in range(0, len(Xte), 256):
                xb = torch.tensor(Xte[i:i+256], dtype=torch.float32, device=device)
                logits.append(net(xb).cpu().numpy())
            pred = np.argmax(np.vstack(logits), axis=1)
        infer_ms = 1000.0 * (time.perf_counter() - t1) / max(len(Xte), 1)
        return pred, "ok", train_time, infer_ms
    except Exception as exc:
        return None, f"skipped/error: {type(exc).__name__}: {str(exc)[:120]}", np.nan, np.nan


def _run_timeseries_baselines(results: Dict[str, Any], config: Config, paths: Dict[str, Path]) -> pd.DataFrame:
    df = results["df"]
    split = results["split"]
    labels = sorted(CLASS_NAMES.keys())
