    for name, (a, b) in mixed_cases.items():
        X_mix = _make_mixed_fault_features(split, a, b, n_mix, config)
        scores = _rf_unknown_score(all_model, X_mix)
        p_cal = _empirical_known_cdf_probability(scores, val_scores_all_known)
        unknown_pred = (scores > all_threshold).astype(int)
        oc_score = -anomaly_model.decision_function(X_mix) if anomaly_model is not None else np.full(len(X_mix), np.nan)
        oc_pred = (oc_score > anomaly_threshold).astype(int) if anomaly_model is not None else np.zeros(len(X_mix), dtype=int)
