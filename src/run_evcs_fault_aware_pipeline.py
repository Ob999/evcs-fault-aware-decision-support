def split_by_time(X: pd.DataFrame, y: pd.Series, meta: pd.DataFrame, config: Config):
    """Chronological split by window position with boundary-buffer exclusion.

    The buffer removes windows close to train/validation and validation/test
    transition points, reducing the chance that highly adjacent overlapping
    windows appear in different partitions.
    """
    n_steps = int(config.n_days * 24 * 60 / config.dt_minutes)
    val_step = int(config.val_fraction_start * n_steps)
    test_step = int(config.test_fraction_start * n_steps)

    buffer_steps = max(config.window_steps, getattr(config, "split_boundary_buffer_steps", config.window_steps))

    meta2 = meta.copy()
    meta2["window_start_step"] = meta2["window_end_step"] - config.window_steps + 1

    train_mask = meta2["window_end_step"] < (val_step - buffer_steps)

    val_mask = (
        (meta2["window_start_step"] >= (val_step + buffer_steps)) &
        (meta2["window_end_step"] < (test_step - buffer_steps))
    )

    test_mask = meta2["window_start_step"] >= (test_step + buffer_steps)

    return {
        "X_train": X.loc[train_mask].reset_index(drop=True),
        "y_train": y.loc[train_mask].reset_index(drop=True),
        "meta_train": meta.loc[train_mask].reset_index(drop=True),
        "X_val": X.loc[val_mask].reset_index(drop=True),
        "y_val": y.loc[val_mask].reset_index(drop=True),
        "meta_val": meta.loc[val_mask].reset_index(drop=True),
        "X_test": X.loc[test_mask].reset_index(drop=True),
        "y_test": y.loc[test_mask].reset_index(drop=True),
        "meta_test": meta.loc[test_mask].reset_index(drop=True),
    }


