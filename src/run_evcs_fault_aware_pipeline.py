    rows[-1]["input_representation"] = "raw sequence flattened"

    # Dependency-free ROCKET-style random convolution baseline.
    t0 = time.perf_counter()
    Xr_fit, meta = _rocket_style_transform(Xseq_fit, config)
    Xr_test, _ = _rocket_style_transform(Xseq_test, config, n_kernels=meta["n_kernels"])
    transform_time = time.perf_counter() - t0
    rocket_clf = Pipeline([
        ("scaler", StandardScaler()),
        ("model", LogisticRegression(max_iter=1200, class_weight="balanced", random_state=config.random_seed, n_jobs=-1)),
    ])
