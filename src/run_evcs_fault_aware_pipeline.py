        rows.append({"explanation_method": "Native tree impurity importance", "top_source": np.nan, "top_source_importance": np.nan, "source_ranking": "", "agreement_with_native_top": np.nan, "rank_correlation_with_native": np.nan, "status": f"skipped/error: {type(exc).__name__}"})

