def simulate_evcs_operation(config: Config) -> pd.DataFrame:
    """Generate healthy renewable-powered EVCS time-series data.

    The simulation is intentionally transparent rather than highly detailed.
    It creates physically plausible relationships among PV, EV load, BESS SOC,
    grid import, voltage/current, THD, temperature, port status, and communication.
    """
    rng = set_random_seed(config.random_seed)
    n_steps = int(config.n_days * 24 * 60 / config.dt_minutes)
    dt_h = config.dt_minutes / 60.0

    index = pd.date_range("2026-01-01", periods=n_steps, freq=f"{config.dt_minutes}min")
    df = pd.DataFrame(index=index)
    df["step"] = np.arange(n_steps)
    df["hour"] = df.index.hour + df.index.minute / 60.0
    df["day"] = (df["step"] * config.dt_minutes // (24 * 60)).astype(int)
    df["dow"] = df.index.dayofweek
    df["is_weekend"] = (df["dow"] >= 5).astype(int)
    long_horizon_effects = bool(getattr(config, "enable_long_horizon_effects", False))
    age_fraction = np.linspace(0.0, 1.0, n_steps)

    # Ambient temperature: daily cycle + slower weather variability. In the
    # extended-horizon mode, add a smooth seasonal component so that the station
    # experiences changing ambient conditions across months.
    daily_temp = 29 + 6 * np.sin(2 * np.pi * (df["hour"].values - 14) / 24)
    slow_weather = rng.normal(0, 1.5, size=config.n_days)
    if long_horizon_effects:
        seasonal_temp = 5.0 * np.sin(2 * np.pi * (df.index.dayofyear.values - 120) / 365.0)
    else:
        seasonal_temp = 0.0
    df["ambient_temp_c"] = daily_temp + seasonal_temp + slow_weather[df["day"].values] + rng.normal(0, 0.4, n_steps)

    # PV generation: daylight bell-shaped curve multiplied by day-level weather.
    # In the extended-horizon mode, the weather regime is explicitly represented
    # and the PV amplitude includes seasonal irradiance variation.
    daylight = np.sin(np.pi * (df["hour"].values - 6) / 12)
    daylight = np.clip(daylight, 0, None)
    if long_horizon_effects:
        weather_regimes = np.array(["clear", "partly_cloudy", "cloudy", "low_irradiance"])
        weather_probs = np.array([0.38, 0.34, 0.20, 0.08])
        day_weather = rng.choice(weather_regimes, size=config.n_days, p=weather_probs)
        cloud_ranges = {
            "clear": (0.90, 1.08),
            "partly_cloudy": (0.68, 0.92),
            "cloudy": (0.42, 0.70),
            "low_irradiance": (0.22, 0.48),
        }
        day_cloud = np.array([rng.uniform(*cloud_ranges[w]) for w in day_weather])
        day_numbers = np.arange(config.n_days)
        seasonal_irradiance = 0.88 + 0.22 * np.sin(2 * np.pi * (day_numbers + 30) / 365.0)
        day_cloud = np.clip(day_cloud * seasonal_irradiance, 0.10, 1.15)
        df["weather_regime"] = day_weather[df["day"].values]
        df["seasonal_irradiance_factor"] = seasonal_irradiance[df["day"].values]
    else:
        day_cloud = rng.uniform(0.55, 1.05, size=config.n_days)
        df["weather_regime"] = "nominal"
        df["seasonal_irradiance_factor"] = 1.0
    fast_cloud = np.clip(rng.normal(1.0, 0.08, n_steps), 0.65, 1.20)
    df["pv_available_kw"] = config.pv_capacity_kw * (daylight ** 1.7) * day_cloud[df["day"].values] * fast_cloud

    # EV active ports: morning, midday, and evening charging behaviour.
    h = df["hour"].values
    demand_shape = (
        0.8 * np.exp(-0.5 * ((h - 8.5) / 2.0) ** 2)
        + 1.1 * np.exp(-0.5 * ((h - 13.0) / 2.8) ** 2)
        + 1.3 * np.exp(-0.5 * ((h - 18.0) / 2.5) ** 2)
        + 0.18
    )
    if long_horizon_effects:
        # Weekdays retain commute/workplace peaks; weekends shift demand toward
        # later midday/leisure charging. A slow month-scale component represents
        # demand seasonality and gradual load growth across the horizon.
        weekend_midday = 0.45 * np.exp(-0.5 * ((h - 15.0) / 3.5) ** 2)
        demand_shape = demand_shape + weekend_midday * df["is_weekend"].values
        weekend_factor = np.where(df["is_weekend"].values == 1, 0.88, 1.05)
        day_numbers_step = df["day"].values.astype(float)
        demand_seasonality = 1.0 + 0.12 * np.sin(2 * np.pi * (day_numbers_step + 20) / 180.0)
        demand_growth = 1.0 + 0.10 * age_fraction
    else:
        weekend_factor = np.where(df["is_weekend"].values == 1, 0.75, 1.0)
        demand_seasonality = 1.0
        demand_growth = 1.0
    expected_active_ports = np.clip(demand_shape * weekend_factor * demand_seasonality * demand_growth * (config.n_ports / 3.2), 0, config.n_ports)
    active_ports = rng.poisson(expected_active_ports)
    active_ports = np.clip(active_ports, 0, config.n_ports)
    df["active_ports"] = active_ports
    per_port_power = rng.normal(43.0, 8.0, n_steps)
    per_port_power = np.clip(per_port_power, 15.0, 65.0)
    requested_power = active_ports * per_port_power
    requested_power = np.clip(requested_power, 0, config.station_power_kw)
    df["ev_requested_power_kw"] = requested_power

    # BESS dispatch heuristic: discharge during deficit/peak, charge from surplus PV.
    soc = np.zeros(n_steps)
    p_ch = np.zeros(n_steps)
    p_dis = np.zeros(n_steps)
    p_pv_used = np.zeros(n_steps)
    p_grid = np.zeros(n_steps)
    soc[0] = config.bess_initial_soc

    for i in range(n_steps):
        pv = df["pv_available_kw"].iloc[i]
        demand = df["ev_requested_power_kw"].iloc[i] + config.aux_load_kw
        hour = df["hour"].iloc[i]
        current_soc = soc[i - 1] if i > 0 else soc[0]

        # Use PV directly first.
        pv_direct = min(pv, demand)
        remaining = max(demand - pv_direct, 0.0)
        surplus_pv = max(pv - pv_direct, 0.0)

        # Charge BESS from surplus PV if possible.
        max_ch_energy_room_kw = max((config.bess_max_soc - current_soc) * config.bess_capacity_kwh / (dt_h * config.bess_eta_ch), 0.0)
        charge_kw = min(surplus_pv, config.bess_max_charge_kw, max_ch_energy_room_kw)

        # Discharge during deficit, especially peak hours.
        peak_weight = 1.0 if (7 <= hour <= 22) else 0.35
        max_dis_energy_kw = max((current_soc - config.bess_min_soc) * config.bess_capacity_kwh * config.bess_eta_dis / dt_h, 0.0)
        discharge_kw = min(remaining * peak_weight, config.bess_max_discharge_kw, max_dis_energy_kw)

        grid_kw = max(remaining - discharge_kw, 0.0)
        p_ch[i] = charge_kw
        p_dis[i] = discharge_kw
        p_pv_used[i] = pv_direct + charge_kw
        p_grid[i] = grid_kw

        if i < n_steps - 1:
            soc[i + 1] = current_soc + (config.bess_eta_ch * charge_kw * dt_h / config.bess_capacity_kwh) - (
                discharge_kw * dt_h / (config.bess_eta_dis * config.bess_capacity_kwh)
            )
            soc[i + 1] = np.clip(soc[i + 1], config.bess_min_soc, config.bess_max_soc)

    df["bess_charge_kw"] = p_ch
    df["bess_discharge_kw"] = p_dis
    df["bess_power_kw"] = p_dis - p_ch
    df["bess_soc"] = soc
    df["pv_used_kw"] = p_pv_used
    df["grid_import_kw"] = p_grid

    # Delivered charging power under healthy operation.
    station_supply_limit = config.station_power_kw
    df["ev_delivered_power_kw"] = np.minimum(df["ev_requested_power_kw"].values, station_supply_limit)
    df["unmet_power_kw"] = np.maximum(df["ev_requested_power_kw"].values - df["ev_delivered_power_kw"].values, 0.0)

    # Electrical measurement proxies.
    load_factor = safe_divide(df["ev_delivered_power_kw"].values, max(config.station_power_kw, 1.0))
    voltage_noise = rng.normal(0, 1.2, n_steps)
    df["voltage_v"] = config.nominal_voltage_v - 0.025 * df["grid_import_kw"].values + voltage_noise
    df["current_a"] = safe_divide(df["ev_delivered_power_kw"].values * 1000, np.sqrt(3) * df["voltage_v"].values)
    df["current_imbalance"] = np.clip(rng.normal(0.015 + 0.01 * load_factor, 0.006, n_steps), 0, 0.08)
    df["thd_i"] = np.clip(0.025 + 0.045 * load_factor + rng.normal(0, 0.006, n_steps), 0.005, 0.18)
    df["power_balance_residual_kw"] = rng.normal(0, 1.2, n_steps)

    # Thermal model: first-order inertia around load-dependent steady state.
    temp = np.zeros(n_steps)
    temp[0] = df["ambient_temp_c"].iloc[0] + 4.0
    for i in range(1, n_steps):
        ambient = df["ambient_temp_c"].iloc[i]
        steady = ambient + 8.0 + 38.0 * load_factor[i] + 4.0 * df["thd_i"].iloc[i]
        temp[i] = 0.92 * temp[i - 1] + 0.08 * steady + rng.normal(0, 0.15)
    df["converter_temp_c"] = temp
    df["thermal_residual_c"] = rng.normal(0, 0.6, n_steps)

    # Session/port indicators.
    df["port_available_ratio"] = 1.0
    df["failed_session_flag"] = (rng.random(n_steps) < 0.002).astype(int)
    df["interrupted_session_flag"] = (rng.random(n_steps) < 0.0015).astype(int)
    df["session_duration_proxy_min"] = np.where(active_ports > 0, rng.normal(28, 12, n_steps), 0)
    df["session_duration_proxy_min"] = np.clip(df["session_duration_proxy_min"], 0, 90)

    # Communication/data quality.
    df["packet_loss_rate"] = np.clip(rng.normal(0.006, 0.004, n_steps), 0, 0.06)
    df["missing_data_rate"] = np.clip(rng.normal(0.003, 0.002, n_steps), 0, 0.04)
    df["status_inconsistency_flag"] = (rng.random(n_steps) < 0.001).astype(int)
    df["stale_measurement_flag"] = (rng.random(n_steps) < 0.001).astype(int)

    # Grid-side proxies.
    df["voltage_deviation_pct"] = np.abs(df["voltage_v"] - config.nominal_voltage_v) / config.nominal_voltage_v * 100
    df["transformer_loading_pct"] = np.clip(35 + 65 * safe_divide(df["grid_import_kw"].values, config.station_power_kw) + rng.normal(0, 3.0, n_steps), 0, 125)
    df["pv_utilization_inst"] = safe_divide(df["pv_used_kw"].values, df["pv_available_kw"].values)
    df["pv_utilization_inst"] = np.clip(df["pv_utilization_inst"], 0, 1)

    if long_horizon_effects:
        # Long-term drift and aging proxies. These are kept as healthy operating
        # evolution until explicit faults are injected, allowing the extended
        # benchmark to test whether models remain stable under slow normal drift.
        active_mask = (df["active_ports"].values > 0).astype(float)
        df["converter_temp_c"] += (1.0 + 3.0 * load_factor) * age_fraction
        df["thermal_residual_c"] += 1.8 * age_fraction
        df["thd_i"] = np.clip(df["thd_i"].values + 0.006 * age_fraction, 0.005, 0.22)
        df["current_imbalance"] = np.clip(df["current_imbalance"].values + 0.004 * age_fraction, 0, 0.12)
        df["packet_loss_rate"] = np.clip(df["packet_loss_rate"].values + 0.006 * age_fraction, 0, 0.08)
        df["missing_data_rate"] = np.clip(df["missing_data_rate"].values + 0.004 * age_fraction, 0, 0.06)
        df["port_available_ratio"] = np.clip(df["port_available_ratio"].values - 0.02 * age_fraction * active_mask, 0.92, 1.0)
        extra_fail_prob = 0.001 + 0.006 * age_fraction * active_mask
        extra_interrupt_prob = 0.001 + 0.004 * age_fraction * active_mask
        df["failed_session_flag"] = np.maximum(df["failed_session_flag"].values, (rng.random(n_steps) < extra_fail_prob).astype(int))
        df["interrupted_session_flag"] = np.maximum(df["interrupted_session_flag"].values, (rng.random(n_steps) < extra_interrupt_prob).astype(int))
        df["voltage_deviation_pct"] = np.abs(df["voltage_v"] - config.nominal_voltage_v) / config.nominal_voltage_v * 100

    # Labels and event IDs; fault injection happens later.
    df["label"] = 0
    df["is_anomaly"] = 0
    df["event_id"] = -1
    df["event_phase"] = "healthy"

    return df


