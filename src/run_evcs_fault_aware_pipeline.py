def _event_level_detection_details(events_df: pd.DataFrame, maintenance_decisions: pd.DataFrame, config: Config, *, segment: str = "test") -> pd.DataFrame:
    """Compute event-oriented detection metrics from window-level decisions."""
    if events_df is None or events_df.empty:
        return pd.DataFrame()
    ev = events_df.copy()
    if "segment" in ev.columns:
        ev = ev.loc[ev["segment"].astype(str).str.lower() == segment.lower()].copy()
    if ev.empty:
        return pd.DataFrame()
    md = maintenance_decisions.copy()
    if "window_end" in md.columns:
        md["window_end"] = pd.to_datetime(md["window_end"])
    action_states = {"Warning", "Degraded", "Critical"}
    rows: List[Dict[str, Any]] = []
    for _, row in ev.iterrows():
        eid = int(row["event_id"])
        cls = int(row["class_id"])
        start_time = pd.to_datetime(row["start_time"])
        end_time = pd.to_datetime(row["end_time"])
        duration_min = float(row.get("duration_hours", 0.0)) * 60.0
        md_event = md.loc[md["dominant_event_id"] == eid].copy() if "dominant_event_id" in md.columns else pd.DataFrame()
        action_rows = md_event.loc[md_event["health_state"].isin(action_states)].copy() if not md_event.empty else pd.DataFrame()
        if not action_rows.empty:
            detection_time = pd.to_datetime(action_rows["window_end"]).min()
            detected = bool(detection_time <= end_time)
            detection_delay_min = max((detection_time - start_time).total_seconds() / 60.0, 0.0)
            detection_lead_time_min = max((end_time - detection_time).total_seconds() / 60.0, 0.0)
            first_state = str(action_rows.sort_values("window_end")["health_state"].iloc[0])
        else:
            detected = False
            detection_delay_min = np.nan
            detection_lead_time_min = np.nan
            first_state = "Missed"
        rows.append({
            "event_id": eid,
            "class_id": cls,
            "class_name": CLASS_NAMES.get(cls, str(cls)),
            "segment": segment,
            "start_time": str(start_time),
            "end_time": str(end_time),
            "duration_min": duration_min,
            "detected": detected,
            "first_action_state": first_state,
            "detection_delay_min": detection_delay_min,
            "detection_lead_time_min": detection_lead_time_min,
            "n_action_windows_for_event": int(len(action_rows)),
        })
    return pd.DataFrame(rows)


def _summarize_event_level_by_class(event_details: pd.DataFrame) -> pd.DataFrame:
    if event_details.empty:
        return pd.DataFrame()
    rows = []
    for cls, grp in event_details.groupby("class_name", sort=False):
        detected = grp["detected"].astype(bool)
        delay = grp.loc[detected, "detection_delay_min"]
        lead = grp.loc[detected, "detection_lead_time_min"]
        rows.append({
            "fault_class": cls,
            "n_test_events": int(len(grp)),
            "detected_events": int(detected.sum()),
            "missed_events": int((~detected).sum()),
            "event_detection_rate": safe_divide(float(detected.sum()), float(len(grp))),
            "mean_detection_delay_min": float(delay.mean()) if len(delay) else np.nan,
            "median_detection_delay_min": float(delay.median()) if len(delay) else np.nan,
            "mean_detection_lead_time_min": float(lead.mean()) if len(lead) else np.nan,
            "median_detection_lead_time_min": float(lead.median()) if len(lead) else np.nan,
        })
    out = pd.DataFrame(rows)
    det = event_details["detected"].astype(bool)
    overall = pd.DataFrame([{
        "fault_class": "Overall",
        "n_test_events": int(len(event_details)),
        "detected_events": int(det.sum()),
        "missed_events": int((~det).sum()),
        "event_detection_rate": safe_divide(float(det.sum()), float(len(event_details))),
        "mean_detection_delay_min": float(event_details.loc[det, "detection_delay_min"].mean()) if det.any() else np.nan,
        "median_detection_delay_min": float(event_details.loc[det, "detection_delay_min"].median()) if det.any() else np.nan,
        "mean_detection_lead_time_min": float(event_details.loc[det, "detection_lead_time_min"].mean()) if det.any() else np.nan,
        "median_detection_lead_time_min": float(event_details.loc[det, "detection_lead_time_min"].median()) if det.any() else np.nan,
    }])
    return pd.concat([out, overall], ignore_index=True)


