def evaluate_operational_impact(
    df: pd.DataFrame,
    split: Dict[str, Any],
    maintenance_decisions: pd.DataFrame,
    config: Config,
    paths: Dict[str, Path],
):
    """Scenario-based comparison of reactive and predictive maintenance.

    This is not a field-validated maintenance-savings estimate. It reports an
    explicit scenario assumption: when a fault event is detected before the end
    of the event, a predictive policy reduces the remaining downtime/unmet energy
    by configurable factors.
    """
    test_start_step = int(config.test_fraction_start * len(df))
    df_test = df.loc[df["step"] >= test_start_step].copy()
    dt_h = config.dt_minutes / 60.0

    # Reactive metrics directly from the simulated faulty time series.
    total_hours = len(df_test) * dt_h
    reactive_unavailable_hours = float(np.sum(1.0 - df_test["port_available_ratio"].values) * dt_h)
    reactive_failed_sessions = int(df_test["failed_session_flag"].sum() + df_test["interrupted_session_flag"].sum())
    reactive_unmet_energy = float(np.sum(df_test["unmet_power_kw"].values) * dt_h)
    reactive_grid_import = float(np.sum(df_test["grid_import_kw"].values) * dt_h)
    reactive_pv_util = float(np.sum(df_test["pv_used_kw"].values) / (np.sum(df_test["pv_available_kw"].values) + 1e-9))
    reactive_bess_throughput = float(np.sum(np.abs(df_test["bess_power_kw"].values)) * dt_h)
    reactive_availability = float(1.0 - reactive_unavailable_hours / max(total_hours, 1e-9))

    # Predictive scenario from event-level detection.
    event_metrics = []
    predictive_unavailable_hours = reactive_unavailable_hours
    predictive_unmet_energy = reactive_unmet_energy

    test_events = df_test.loc[df_test["event_id"] >= 0, "event_id"].unique()
    for eid in test_events:
        event_df = df_test.loc[df_test["event_id"] == eid]
        if event_df.empty:
            continue
        event_start_time = event_df.index.min()
        event_end_time = event_df.index.max()
        event_label = int(event_df["label"].mode().iloc[0])

        md_event = maintenance_decisions.loc[maintenance_decisions["dominant_event_id"] == eid].copy()
        if md_event.empty:
            detected = False
            detection_time = pd.NaT
            delay_h = np.nan
        else:
            action_mask = md_event["health_state"].isin(["Warning", "Degraded", "Critical"])
            if action_mask.any():
                detected = True
                detection_time = pd.to_datetime(md_event.loc[action_mask, "window_end"]).min()
                delay_h = max((detection_time - event_start_time).total_seconds() / 3600.0, 0.0)
            else:
                detected = False
                detection_time = pd.NaT
                delay_h = np.nan

        event_unavailable_h = float(np.sum(1.0 - event_df["port_available_ratio"].values) * dt_h)
        event_unmet_kwh = float(np.sum(event_df["unmet_power_kw"].values) * dt_h)

        if detected and pd.notna(detection_time) and detection_time < event_end_time:
            remaining_fraction = max((event_end_time - detection_time).total_seconds(), 0) / max((event_end_time - event_start_time).total_seconds(), 1)
            reducible_unavailable = event_unavailable_h * remaining_fraction
            reducible_unmet = event_unmet_kwh * remaining_fraction
            predictive_unavailable_hours -= config.predictive_remaining_downtime_reduction * reducible_unavailable
            predictive_unmet_energy -= config.predictive_remaining_unmet_energy_reduction * reducible_unmet

        event_metrics.append({
            "event_id": int(eid),
            "class_id": event_label,
            "class_name": CLASS_NAMES[event_label],
            "detected": bool(detected),
            "detection_delay_h": delay_h,
            "event_unavailable_h": event_unavailable_h,
            "event_unmet_energy_kwh": event_unmet_kwh,
        })

    predictive_unavailable_hours = max(predictive_unavailable_hours, 0.0)
    predictive_unmet_energy = max(predictive_unmet_energy, 0.0)
    predictive_availability = float(1.0 - predictive_unavailable_hours / max(total_hours, 1e-9))

    event_df_out = pd.DataFrame(event_metrics)
    event_df_out.to_csv(paths["tables"] / "table_08b_event_detection_summary.csv", index=False)

    rows = [
        {
            "policy": "Reactive maintenance",
            "port_availability_proxy": reactive_availability,
            "unavailable_port_hours": reactive_unavailable_hours,
            "failed_or_interrupted_session_count": reactive_failed_sessions,
            "unmet_energy_kwh": reactive_unmet_energy,
            "grid_import_kwh": reactive_grid_import,
            "pv_utilization_ratio": reactive_pv_util,
            "bess_throughput_kwh": reactive_bess_throughput,
            "maintenance_actions_triggered": 0,
        },
        {
            "policy": "Predictive maintenance scenario",
            "port_availability_proxy": predictive_availability,
            "unavailable_port_hours": predictive_unavailable_hours,
            "failed_or_interrupted_session_count": reactive_failed_sessions,  # unchanged; session logs are observed
            "unmet_energy_kwh": predictive_unmet_energy,
            "grid_import_kwh": reactive_grid_import,
            "pv_utilization_ratio": reactive_pv_util,
            "bess_throughput_kwh": reactive_bess_throughput,
            "maintenance_actions_triggered": int(maintenance_decisions["health_state"].isin(["Warning", "Degraded", "Critical"]).sum()),
        },
    ]
    impact_df = pd.DataFrame(rows)
    impact_df.to_csv(paths["tables"] / "table_09_operational_impact.csv", index=False)

    # Operational impact figure: use selected normalized metrics for readability.
    plot_metrics = ["port_availability_proxy", "unavailable_port_hours", "unmet_energy_kwh"]
    plot_df = impact_df.set_index("policy")[plot_metrics].copy()
    # Normalize each metric by reactive value except availability.
    norm_df = plot_df.copy()
    for col in ["unavailable_port_hours", "unmet_energy_kwh"]:
        norm_df[col] = norm_df[col] / max(norm_df.iloc[0][col], 1e-9)
    # Availability already in 0-1.

    fig, ax = plt.subplots(figsize=(9, 5))
    x = np.arange(len(plot_metrics))
    width = 0.35
    ax.bar(x - width / 2, norm_df.iloc[0].values, width, label="Reactive")
    ax.bar(x + width / 2, norm_df.iloc[1].values, width, label="Predictive scenario")
    ax.set_xticks(x)
    ax.set_xticklabels(["Availability", "Unavailable hours\n(relative)", "Unmet energy\n(relative)"])
    ax.set_ylabel("Metric value")
    ax.set_title("Operational Impact Comparison")
    ax.legend()
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_08_operational_impact.png", dpi=300)
    plt.close(fig)

    return {"operational_impact": impact_df, "event_detection": event_df_out}


