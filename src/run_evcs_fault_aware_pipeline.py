def run_fault_aware_energy_management_analysis(results: Dict[str, Any], config: Config, paths: Dict[str, Path]) -> Dict[str, pd.DataFrame]:
    """Evaluate whether predictive decisions change grid/DER outcomes.

    This analysis strengthens the renewable-powered/grid-interactive claim by
    comparing the original reliability-only response with a fault-aware local
    energy-management response that modifies charger derating, BESS support, PV
    use/curtailment, grid-import peaks, and transformer-overload exposure.
    """
    print("\nFault-aware energy-management response analysis")
    print("------------------------------------------------")
    df = results["df"]
    decisions = results["maintenance_decisions"]
    action_model = _get_fault_aware_der_action_model()

    reactive, _ = _evaluate_fault_aware_energy_policy(
        df, decisions, config, "Reactive operation", action_model=None, generic_availability_recovery=0.0, generic_unmet_recovery=0.0
    )
    # Reactive operation has no predictive or DER-control action even though
    # detected events exist in the diagnostic layer.
    reactive["event_detection_rate"] = 0.0
    reactive["effective_energy_action_rate"] = 0.0
    reactive["fault_aware_energy_actions"] = 0
    pm_only, _ = _evaluate_fault_aware_energy_policy(
        df, decisions, config, "Predictive maintenance only", action_model=None,
        generic_availability_recovery=config.predictive_remaining_downtime_reduction,
        generic_unmet_recovery=config.predictive_remaining_unmet_energy_reduction,
    )
    der_policy, class_df = _evaluate_fault_aware_energy_policy(
        df, decisions, config, "Predictive + fault-aware energy management", action_model=action_model,
        der_coupling_scale=1.0, delay_scale=1.0,
    )
    policy_df = pd.DataFrame([reactive, pm_only, der_policy])
    policy_df.to_csv(paths["tables"] / "table_48_fault_aware_energy_management.csv", index=False)
    _make_latex_fault_aware_energy_table(
        policy_df,
        paths["tables"] / "latex_table_fault_aware_energy_management.tex",
        "Comparison of reactive operation, predictive-maintenance-only response, and fault-aware energy-management response.",
        "tab:fault_aware_energy_management",
    )

    action_model.to_csv(paths["tables"] / "table_49a_fault_aware_der_action_assumptions.csv", index=False)
    class_df.to_csv(paths["tables"] / "table_49_der_response_by_fault_type.csv", index=False)
    _make_latex_der_action_table(class_df, paths["tables"] / "latex_table_der_response_by_fault_type.tex")

    sens_rows = []
    scenarios = [
        ("No DER coupling (PM only)", None, 0.0, 1.0),
        ("Mild DER coupling", action_model, 0.50, 1.0),
        ("Nominal DER coupling", action_model, 1.00, 1.0),
        ("Strong DER coupling", action_model, 1.50, 1.0),
        ("Nominal DER coupling with rapid response", action_model, 1.00, 0.50),
    ]
    for name, model, scale, dscale in scenarios:
        if model is None:
            row, _ = _evaluate_fault_aware_energy_policy(
                df, decisions, config, name, action_model=None,
                generic_availability_recovery=config.predictive_remaining_downtime_reduction,
                generic_unmet_recovery=config.predictive_remaining_unmet_energy_reduction,
            )
            row["der_coupling_scale"] = 0.0
            row["delay_scale"] = 1.0
        else:
            row, _ = _evaluate_fault_aware_energy_policy(df, decisions, config, name, action_model=model, der_coupling_scale=scale, delay_scale=dscale)
        sens_rows.append(row)
    sens_df = pd.DataFrame(sens_rows)
    sens_df.to_csv(paths["tables"] / "table_50_grid_der_coupling_sensitivity.csv", index=False)
    _make_latex_fault_aware_energy_table(
        sens_df,
        paths["tables"] / "latex_table_grid_der_coupling_sensitivity.tex",
        "Sensitivity of operational and energy-management outcomes to DER-coupling strength and response latency.",
        "tab:grid_der_coupling_sensitivity",
    )

    _plot_fault_aware_energy_management(policy_df, paths)
    _plot_grid_import_peak_response(policy_df, paths)

    print("Fault-aware energy-management outputs saved:")
    print("  outputs/tables/table_48_fault_aware_energy_management.csv")
    print("  outputs/tables/table_49_der_response_by_fault_type.csv")
    print("  outputs/tables/table_50_grid_der_coupling_sensitivity.csv")
    print("  outputs/figures/fig_21_fault_aware_energy_management.png")
    print("  outputs/figures/fig_22_grid_import_peak_response.png")
    return {
        "fault_aware_energy_management": policy_df,
        "der_response_by_fault_type": class_df,
        "grid_der_coupling_sensitivity": sens_df,
        "fault_aware_der_action_assumptions": action_model,
    }

