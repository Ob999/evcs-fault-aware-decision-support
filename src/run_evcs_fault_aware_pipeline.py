def _normalize_series(x: np.ndarray) -> np.ndarray:
    x = np.asarray(x, dtype=float)
    lo = np.nanpercentile(x, 5)
    hi = np.nanpercentile(x, 95)
    if hi - lo <= 1e-9:
        return np.zeros_like(x)
    return np.clip((x - lo) / (hi - lo), 0, 1)


def compute_maintenance_risk(
    split: Dict[str, Any],
    supervised_results: Dict[str, Any],
    anomaly_results: Dict[str, Any],
    config: Config,
    paths: Dict[str, Path],
):
    X_test = split["X_test"].reset_index(drop=True)
    y_test = split["y_test"].reset_index(drop=True)
    meta_test = split["meta_test"].reset_index(drop=True)
    model = supervised_results["best_model"]
    y_pred = supervised_results["best_predictions"]

    if hasattr(model, "predict_proba"):
        proba = model.predict_proba(X_test)
        confidence = proba.max(axis=1)
    else:
        confidence = np.ones(len(X_test)) * 0.75

    anomaly_score = anomaly_results["best_anomaly_scores"]
    anomaly_pred = anomaly_results["best_anomaly_predictions"]

    severity = np.array([SEVERITY_MAP.get(int(c), 0.5) for c in y_pred], dtype=float)

    # Persistence over last several windows.
    persistence = pd.Series(anomaly_pred).rolling(window=4, min_periods=1).mean().values

    # Operational impact score from selected features.
    impact_raw = (
        0.35 * _normalize_series(X_test.get("session__unmet_energy_kwh_window", pd.Series(np.zeros(len(X_test)))).values)
        + 0.25 * _normalize_series(1.0 - X_test.get("session__port_available_ratio__mean", pd.Series(np.ones(len(X_test)))).values)
        + 0.20 * _normalize_series(X_test.get("grid__transformer_loading_pct__mean", pd.Series(np.zeros(len(X_test)))).values)
        + 0.20 * _normalize_series(X_test.get("comm__data_quality_risk", pd.Series(np.zeros(len(X_test)))).values)
    )
    impact = np.clip(impact_raw, 0, 1)

    anomaly_norm = _normalize_series(anomaly_score)

    risk = (
        0.35 * severity
        + 0.20 * confidence
        + 0.20 * persistence
        + 0.15 * impact
        + 0.10 * anomaly_norm
    )
    # Healthy predictions should not become high risk only because confidence is high.
    risk = np.where(y_pred == 0, np.minimum(risk, 0.24 + 0.20 * anomaly_pred), risk)
    risk = np.clip(risk, 0, 1)

    def state_and_action(r: float) -> Tuple[str, str]:
        if r < 0.25:
            return "Healthy", "Normal monitoring"
        if r < 0.45:
            return "Warning", "Closer observation / schedule inspection"
        if r < 0.70:
            return "Degraded", "Plan maintenance / consider derating"
        return "Critical", "Immediate intervention / isolate affected port"

    states, actions = zip(*[state_and_action(r) for r in risk])

    out = meta_test.copy()
    out["true_label"] = y_test.values
    out["true_label_name"] = [CLASS_NAMES[int(v)] for v in y_test.values]
    out["predicted_label"] = y_pred
    out["predicted_label_name"] = [CLASS_NAMES[int(v)] for v in y_pred]
    out["diagnostic_confidence"] = confidence
    out["anomaly_score"] = anomaly_score
    out["anomaly_pred"] = anomaly_pred
    out["severity_component"] = severity
    out["persistence_component"] = persistence
    out["operational_impact_component"] = impact
    out["risk_score"] = risk
    out["health_state"] = states
    out["maintenance_action"] = actions

    out.to_csv(paths["data"] / "test_maintenance_decisions.csv", index=False)

    # Summary table.
    summary = (
        out.groupby(["health_state", "maintenance_action"])
        .size()
        .reset_index(name="n_windows")
        .sort_values("n_windows", ascending=False)
    )
    summary["fraction_of_test_windows"] = summary["n_windows"] / len(out)
    summary.to_csv(paths["tables"] / "table_08_maintenance_outcomes.csv", index=False)

    # Risk timeline figure.
    fig, ax = plt.subplots(figsize=(11, 4))
    ax.plot(pd.to_datetime(out["window_end"]), out["risk_score"], linewidth=1.0)
    ax.axhline(0.25, linestyle="--", linewidth=0.8)
    ax.axhline(0.45, linestyle="--", linewidth=0.8)
    ax.axhline(0.70, linestyle="--", linewidth=0.8)
    ax.set_ylabel("Risk score")
    ax.set_xlabel("Time")
    ax.set_title("Predictive-Maintenance Risk Timeline")
    fig.autofmt_xdate()
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_07_risk_timeline.png", dpi=300)
    plt.close(fig)

    return out


