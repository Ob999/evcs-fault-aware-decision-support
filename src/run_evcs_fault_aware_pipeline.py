def compute_explainability(best_model: Pipeline, split: Dict[str, Any], y_pred: np.ndarray, config: Config, paths: Dict[str, Path]):
    """Compute global and class-wise explainability outputs.

    This improved version adds two manuscript-friendly figures:
      - fig_06a_grouped_feature_source_importance.png
      - fig_06b_classwise_root_cause_source_map.png

    These figures are intended to show that the framework uses multi-source
    evidence and that the dominant root-cause indicators are class-specific,
    rather than allowing one feature family to dominate the global ranking.
    """
    X_test, y_test = split["X_test"], split["y_test"]

    feature_names = X_test.columns.tolist()
    importance = None
    method = None

    def feature_source(feature: str) -> str:
        prefix = str(feature).split("__", 1)[0]
        readable = {
            "elec": "Electrical",
            "thermal": "Thermal",
            "session": "Session/port",
            "grid": "Grid/PV/BESS",
            "comm": "Communication/data",
        }
        return readable.get(prefix, prefix)

    # Try native importance for tree-based model inside a pipeline.
    final_model = best_model.named_steps.get("model", best_model)
    if hasattr(final_model, "feature_importances_"):
        importance = final_model.feature_importances_
        method = "native_feature_importance"
    else:
        # Use permutation importance on a subset to keep runtime reasonable.
        subset_size = min(1200, len(X_test))
        rng = np.random.default_rng(config.random_seed)
        subset_idx = rng.choice(len(X_test), size=subset_size, replace=False) if len(X_test) > subset_size else np.arange(len(X_test))
        result = permutation_importance(
            best_model, X_test.iloc[subset_idx], y_test.iloc[subset_idx],
            n_repeats=5, random_state=config.random_seed, scoring="f1_macro", n_jobs=-1,
        )
        importance = result.importances_mean
        method = "permutation_importance"

    imp_df = pd.DataFrame({
        "feature": feature_names,
        "source_group": [feature_source(f) for f in feature_names],
        "importance": importance,
        "method": method,
    }).sort_values("importance", ascending=False)
    imp_df.to_csv(paths["tables"] / "table_07_top_features.csv", index=False)

    # Original global feature importance figure, kept for traceability.
    top = imp_df.head(20).iloc[::-1]
    fig, ax = plt.subplots(figsize=(8, 7))
    ax.barh(top["feature"], top["importance"])
    ax.set_xlabel("Importance")
    ax.set_title("Top Global Diagnostic Features")
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_06_feature_importance.png", dpi=300)
    plt.close(fig)

    # New Figure 6a: grouped feature-source importance.
    grouped_imp = (
        imp_df.groupby("source_group", as_index=False)["importance"]
        .sum()
        .sort_values("importance", ascending=False)
    )
    total_imp = grouped_imp["importance"].sum()
    grouped_imp["relative_importance"] = grouped_imp["importance"] / (total_imp + 1e-12)
    grouped_imp.to_csv(paths["tables"] / "table_07c_grouped_feature_source_importance.csv", index=False)

    fig, ax = plt.subplots(figsize=(8, 4.8))
    ax.bar(grouped_imp["source_group"], grouped_imp["relative_importance"])
    ax.set_ylabel("Relative summed importance")
    ax.set_xlabel("Feature source")
    ax.set_title("Grouped Diagnostic Importance by Feature Source")
    ax.set_ylim(0, max(0.05, float(grouped_imp["relative_importance"].max()) * 1.20))
    ax.tick_params(axis="x", rotation=20)
    for i, v in enumerate(grouped_imp["relative_importance"]):
        ax.text(i, v + 0.01, f"{v:.2f}", ha="center", va="bottom", fontsize=9)
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_06a_grouped_feature_source_importance.png", dpi=300)
    plt.close(fig)

    # Class-wise mean deviation from healthy baseline to support root-cause interpretation.
    X_train = split["X_train"]
    y_train = split["y_train"]
    healthy_baseline = X_train.loc[y_train == 0].median(numeric_only=True)
    healthy_iqr = (X_train.loc[y_train == 0].quantile(0.75) - X_train.loc[y_train == 0].quantile(0.25)).abs()
    global_std = X_train.std(numeric_only=True).abs()
    # Use a stability floor based on global variability. This avoids unrealistically large
    # robust shifts for features that are constant during healthy operation but informative
    # during faults, such as unmet power or binary communication flags.
    robust_scale = pd.concat(
        [
            healthy_iqr,
            0.25 * global_std,
            pd.Series(1e-3, index=healthy_iqr.index),
        ],
        axis=1,
    ).max(axis=1)

    class_expl_rows = []
    source_shift_rows = []
    all_sources = ["Electrical", "Thermal", "Session/port", "Grid/PV/BESS", "Communication/data"]

    for cls in sorted(CLASS_NAMES.keys()):
        mask = (y_test.values == cls)
        if mask.sum() < 3:
            continue
        cls_median = X_test.loc[mask].median(numeric_only=True)
        robust_shift = ((cls_median - healthy_baseline) / robust_scale).abs().sort_values(ascending=False)

        # Keep top individual root-cause features for the table.
        for rank, (feat, val) in enumerate(robust_shift.head(8).items(), start=1):
            class_expl_rows.append({
                "class_id": cls,
                "class_name": CLASS_NAMES[cls],
                "rank": rank,
                "feature": feat,
                "source_group": feature_source(feat),
                "abs_robust_shift_vs_healthy": float(val),
            })

        # Source-level class-wise root-cause strength: mean of the top five shifts within each source.
        shift_df = pd.DataFrame({
            "feature": robust_shift.index,
            "source_group": [feature_source(f) for f in robust_shift.index],
            "abs_robust_shift_vs_healthy": robust_shift.values,
        })
        for src_name in all_sources:
            vals = shift_df.loc[shift_df["source_group"] == src_name, "abs_robust_shift_vs_healthy"].sort_values(ascending=False)
            src_strength = float(vals.head(5).mean()) if len(vals) > 0 else 0.0
            source_shift_rows.append({
                "class_id": cls,
                "class_name": CLASS_NAMES[cls],
                "source_group": src_name,
                "top5_mean_abs_robust_shift": src_strength,
            })

    class_expl_df = pd.DataFrame(class_expl_rows)
    class_expl_df.to_csv(paths["tables"] / "table_07b_classwise_root_cause_features.csv", index=False)

    source_shift_df = pd.DataFrame(source_shift_rows)
    source_shift_df.to_csv(paths["tables"] / "table_07d_classwise_source_shift_map.csv", index=False)

    # New Figure 6b: class-wise root-cause source map. Exclude healthy to focus on faults/anomalies.
    plot_shift = source_shift_df.loc[source_shift_df["class_id"] != 0].copy()
    if not plot_shift.empty:
        pivot = plot_shift.pivot(index="class_name", columns="source_group", values="top5_mean_abs_robust_shift")
        pivot = pivot.reindex(index=[CLASS_NAMES[i] for i in sorted(CLASS_NAMES.keys()) if i != 0])
        pivot = pivot[all_sources]

        # Normalize row-wise for visual interpretability while keeping raw values in the saved table.
        row_max = pivot.max(axis=1).replace(0, 1.0)
        pivot_norm = pivot.div(row_max, axis=0)

        fig, ax = plt.subplots(figsize=(9.2, 5.2))
        im = ax.imshow(pivot_norm.values, aspect="auto")
        ax.set_xticks(np.arange(len(pivot_norm.columns)))
        ax.set_xticklabels(pivot_norm.columns, rotation=25, ha="right")
        ax.set_yticks(np.arange(len(pivot_norm.index)))
        ax.set_yticklabels(pivot_norm.index)
        ax.set_title("Class-Wise Root-Cause Source Map")
        cbar = fig.colorbar(im, ax=ax)
        cbar.set_label("Row-normalized root-cause strength")

        # Annotate each cell with normalized score.
        for i in range(pivot_norm.shape[0]):
            for j in range(pivot_norm.shape[1]):
                ax.text(j, i, f"{pivot_norm.iloc[i, j]:.2f}", ha="center", va="center", fontsize=8)

        fig.tight_layout()
        fig.savefig(paths["figures"] / "fig_06b_classwise_root_cause_source_map.png", dpi=300)
        plt.close(fig)

    return {
        "global_importance": imp_df,
        "grouped_importance": grouped_imp,
        "classwise_explanations": class_expl_df,
        "classwise_source_shift_map": source_shift_df,
    }


