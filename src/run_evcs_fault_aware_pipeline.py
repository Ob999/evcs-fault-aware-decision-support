def run_anomaly_detection(split: Dict[str, Any], config: Config, paths: Dict[str, Path]):
    X_train, y_train = split["X_train"], split["y_train"]
    X_val, y_val = split["X_val"], split["y_val"]
    X_test, y_test = split["X_test"], split["y_test"]
    y_test_bin = (y_test != 0).astype(int).values

    X_train_healthy = X_train.loc[y_train == 0].reset_index(drop=True)
    X_val_healthy = X_val.loc[y_val == 0].reset_index(drop=True)

    # Limit unsupervised training size for fast, reproducible execution.
    # The cap is fixed and reported through the configuration file; it keeps
    # One-Class SVM practical for repeated-seed stability analysis without
    # using any test information.
    if len(X_train_healthy) > config.max_healthy_windows_for_ocsvm:
        X_train_healthy = X_train_healthy.sample(n=config.max_healthy_windows_for_ocsvm, random_state=config.random_seed).reset_index(drop=True)

    # If there is not enough validation healthy data, fall back to train healthy.
    if len(X_val_healthy) < 20:
        X_val_healthy = X_train_healthy.copy()

    models = {
        "IsolationForest": Pipeline([
            ("imputer", SimpleImputer(strategy="median")),
            ("scaler", StandardScaler()),
            ("model", IsolationForest(
                n_estimators=300,
                contamination="auto",
                random_state=config.random_seed,
                n_jobs=-1,
            )),
        ]),
        "OneClassSVM": Pipeline([
            ("imputer", SimpleImputer(strategy="median")),
            ("scaler", StandardScaler()),
            ("model", OneClassSVM(kernel="rbf", gamma="scale", nu=0.05)),
        ]),
    }

    rows = []
    scores_by_model: Dict[str, np.ndarray] = {}
    pred_by_model: Dict[str, np.ndarray] = {}
    thresholds: Dict[str, float] = {}

    for name, model in models.items():
        t0 = time.perf_counter()
        model.fit(X_train_healthy)
        train_time = time.perf_counter() - t0

        # Higher score means more anomalous.
        val_score = -model.decision_function(X_val_healthy)
        test_score = -model.decision_function(X_test)
        threshold = float(np.quantile(val_score, config.anomaly_threshold_quantile))
        pred_bin = (test_score > threshold).astype(int)

        try:
            roc = float(roc_auc_score(y_test_bin, test_score))
        except ValueError:
            roc = np.nan
        try:
            pr = float(average_precision_score(y_test_bin, test_score))
        except ValueError:
            pr = np.nan

        false_alarm = float(((pred_bin == 1) & (y_test_bin == 0)).sum() / max((y_test_bin == 0).sum(), 1))
        detection_rate = float(((pred_bin == 1) & (y_test_bin == 1)).sum() / max((y_test_bin == 1).sum(), 1))
        unknown_mask = (y_test.values == config.unknown_class_for_open_set)
        unknown_recall = float((pred_bin[unknown_mask] == 1).sum() / max(unknown_mask.sum(), 1))

        rows.append({
            "model": name,
            "roc_auc": roc,
            "pr_auc": pr,
            "f1_at_threshold": float(f1_score(y_test_bin, pred_bin, zero_division=0)),
            "precision_at_threshold": float(precision_score(y_test_bin, pred_bin, zero_division=0)),
            "recall_at_threshold": float(recall_score(y_test_bin, pred_bin, zero_division=0)),
            "false_alarm_rate": false_alarm,
            "detection_rate": detection_rate,
            "withheld_unknown_class": CLASS_NAMES[config.unknown_class_for_open_set],
            "unknown_class_recall": unknown_recall,
            "threshold": threshold,
            "train_time_s": train_time,
        })
        scores_by_model[name] = test_score
        pred_by_model[name] = pred_bin
        thresholds[name] = threshold

    df_anom = pd.DataFrame(rows).sort_values("f1_at_threshold", ascending=False)
    df_anom.to_csv(paths["tables"] / "table_05_anomaly_metrics.csv", index=False)

    best_anom_name = df_anom.iloc[0]["model"]
    best_scores = scores_by_model[best_anom_name]
    best_pred = pred_by_model[best_anom_name]

    # Save anomaly scores.
    score_df = split["meta_test"].copy()
    score_df["true_label"] = y_test.values
    score_df["true_label_name"] = [CLASS_NAMES[int(v)] for v in y_test.values]
    score_df["anomaly_score"] = best_scores
    score_df["anomaly_pred"] = best_pred
    score_df.to_csv(paths["data"] / "test_anomaly_scores.csv", index=False)

    # Plot anomaly score distribution.
    fig, ax = plt.subplots(figsize=(8, 5))
    ax.hist(best_scores[y_test_bin == 0], bins=40, alpha=0.7, label="Healthy")
    ax.hist(best_scores[y_test_bin == 1], bins=40, alpha=0.7, label="Fault/anomaly")
    ax.axvline(thresholds[best_anom_name], linestyle="--", label="Threshold")
    ax.set_xlabel("Anomaly score")
    ax.set_ylabel("Window count")
    ax.set_title(f"Anomaly Score Distribution - {best_anom_name}")
    ax.legend()
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_05_anomaly_scores.png", dpi=300)
    plt.close(fig)

    return {
        "anomaly_metrics": df_anom,
        "best_anomaly_model_name": best_anom_name,
        "best_anomaly_model": models[best_anom_name],
        "best_anomaly_scores": best_scores,
        "best_anomaly_predictions": best_pred,
        "best_anomaly_threshold": thresholds[best_anom_name],
    }


