def run_operational_response_sensitivity_analysis(results: Dict[str, Any], config: Config, paths: Dict[str, Path]) -> Dict[str, pd.DataFrame]:
    """Run scenario-based operational-response sensitivity analysis.

    This analysis reframes the operational-impact comparison as a response-surface
    experiment over assumed remediation effectiveness and response latency. It is
    intentionally explicit that these are scenario assumptions, not field-validated
    maintenance savings.
    """
    print("\nOperational-response sensitivity analysis")
    print("-----------------------------------------")

    df = results["df"]
    decisions = results["maintenance_decisions"]

    # ------------------------------------------------------------------
    # 1) Full intervention-effectiveness response surface.
    # ------------------------------------------------------------------
    surface_rows = []
    for downtime_eff in config.intervention_effectiveness_grid:
        for unmet_eff in config.intervention_effectiveness_grid:
            name = f"Unavailable {int(100*downtime_eff)}% / unmet {int(100*unmet_eff)}%"
            row = _operational_response_metrics(
                df,
                decisions,
                config,
                name,
                downtime_reduction=float(downtime_eff),
                unmet_energy_reduction=float(unmet_eff),
                response_delay_min=0.0,
            )
            surface_rows.append(row)
    surface_df = pd.DataFrame(surface_rows)
    surface_df.to_csv(paths["tables"] / "table_27_intervention_effectiveness_surface.csv", index=False)
    _make_latex_operational_response_table(
        surface_df,
        paths["tables"] / "latex_table_intervention_effectiveness_surface.tex",
        "Scenario-based response surface for assumed intervention effectiveness.",
        "tab:intervention_effectiveness_surface",
        include_assumptions=True,
    )
    _plot_intervention_response_surface(surface_df, paths)

    # ------------------------------------------------------------------
    # 2) Decision/maintenance response latency sensitivity.
    # ------------------------------------------------------------------
    latency_rows = []
    for delay in config.decision_latency_minutes:
        row = _operational_response_metrics(
            df,
            decisions,
            config,
            f"Nominal response with {int(delay)} min additional latency",
            downtime_reduction=config.predictive_remaining_downtime_reduction,
            unmet_energy_reduction=config.predictive_remaining_unmet_energy_reduction,
            response_delay_min=float(delay),
        )
        latency_rows.append(row)
    latency_df = pd.DataFrame(latency_rows)
    latency_df.to_csv(paths["tables"] / "table_28_decision_latency_sensitivity.csv", index=False)
    _make_latex_operational_response_table(
        latency_df,
        paths["tables"] / "latex_table_decision_latency_sensitivity.tex",
        "Sensitivity of operational-response outcomes to additional action latency after detection.",
        "tab:decision_latency_sensitivity",
        include_assumptions=False,
    )
    _plot_decision_latency_sensitivity(latency_df, paths)

    # ------------------------------------------------------------------
    # 3) Fault-type-specific action model.
    # ------------------------------------------------------------------
    action_model_df = _get_fault_type_action_model()
    action_model_df.to_csv(paths["tables"] / "table_29_fault_type_action_model.csv", index=False)
    _make_latex_fault_type_action_model(action_model_df, paths["tables"] / "latex_table_fault_type_action_model.tex")

    # ------------------------------------------------------------------
    # 4) Operational-response policy comparison.
    # ------------------------------------------------------------------
    policy_rows = [
        _operational_response_metrics(df, decisions, config, "Low-effectiveness generic response (20/20)", 0.20, 0.20, 0.0),
        _operational_response_metrics(df, decisions, config, "Nominal generic response (50/40)", 0.50, 0.40, 0.0),
        _operational_response_metrics(df, decisions, config, "Optimistic generic response (60/60)", 0.60, 0.60, 0.0),
        _operational_response_metrics(df, decisions, config, "Maintenance crew delay scenario (50/40 + 60 min)", 0.50, 0.40, 60.0),
        _operational_response_metrics(df, decisions, config, "Spare-part delay scenario (50/40 + 120 min)", 0.50, 0.40, 120.0),
        _operational_response_metrics(df, decisions, config, "Fault-type-specific action model", 0.50, 0.40, 0.0, class_action_model=action_model_df),
    ]
    policy_df = pd.DataFrame(policy_rows)
    policy_df.to_csv(paths["tables"] / "table_30_operational_response_policy_comparison.csv", index=False)
    _make_latex_operational_response_table(
        policy_df,
        paths["tables"] / "latex_table_operational_response_policy_comparison.tex",
        "Comparison of alternative operational-response assumptions after predictive-maintenance detection.",
        "tab:operational_response_policy_comparison",
        include_assumptions=True,
    )

    print("Operational-response sensitivity outputs saved:")
    print("  outputs/tables/table_27_intervention_effectiveness_surface.csv")
    print("  outputs/tables/table_28_decision_latency_sensitivity.csv")
    print("  outputs/tables/table_29_fault_type_action_model.csv")
    print("  outputs/tables/table_30_operational_response_policy_comparison.csv")
    print("  outputs/tables/table_48_fault_aware_energy_management.csv")
    print("  outputs/tables/table_49_der_response_by_fault_type.csv")
    print("  outputs/tables/table_50_grid_der_coupling_sensitivity.csv")
    print("  outputs/figures/fig_21_fault_aware_energy_management.png")
    print("  outputs/figures/fig_22_grid_import_peak_response.png")
    print("  outputs/figures/fig_14a_unavailable_response_surface.png")
    print("  outputs/figures/fig_14b_unmet_energy_response_surface.png")
    print("  outputs/figures/fig_15_decision_latency_sensitivity.png")

    return {
        "intervention_effectiveness_surface": surface_df,
        "decision_latency_sensitivity": latency_df,
        "fault_type_action_model": action_model_df,
        "operational_response_policy_comparison": policy_df,
    }


