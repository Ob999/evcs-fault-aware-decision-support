def set_random_seed(seed: int) -> np.random.Generator:
    np.random.seed(seed)
    return np.random.default_rng(seed)


def safe_divide(a: np.ndarray | float, b: np.ndarray | float, eps: float = 1e-9):
    return np.asarray(a) / (np.asarray(b) + eps)


def time_slope(y: np.ndarray) -> float:
    """Least-squares slope over a window; returns 0 for constant/short vectors."""
    y = np.asarray(y, dtype=float)
    if len(y) < 2 or np.all(np.isnan(y)):
        return 0.0
    x = np.arange(len(y), dtype=float)
    mask = np.isfinite(y)
    if mask.sum() < 2:
        return 0.0
    x = x[mask]
    y = y[mask]
    x = x - x.mean()
    denom = np.sum(x ** 2)
    if denom <= 1e-12:
        return 0.0
    return float(np.sum(x * (y - y.mean())) / denom)


def summarize_metrics(y_true: np.ndarray, y_pred: np.ndarray, labels: List[int]) -> Dict[str, float]:
    prec, rec, f1, _ = precision_recall_fscore_support(
        y_true, y_pred, labels=labels, average="macro", zero_division=0
    )
    wprec, wrec, wf1, _ = precision_recall_fscore_support(
        y_true, y_pred, labels=labels, average="weighted", zero_division=0
    )
    return {
        "accuracy": float(accuracy_score(y_true, y_pred)),
        "macro_precision": float(prec),
        "macro_recall": float(rec),
        "macro_f1": float(f1),
        "weighted_precision": float(wprec),
        "weighted_recall": float(wrec),
        "weighted_f1": float(wf1),
    }


