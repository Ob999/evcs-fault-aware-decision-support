def _rf_unknown_score(model: Pipeline, X: pd.DataFrame) -> np.ndarray:
    """Return an open-set unknown score from a probabilistic known-class model."""
    prob = model.predict_proba(X)
    return 1.0 - np.max(prob, axis=1)


def _fpr_at_fixed_tpr(y_true_unknown: np.ndarray, scores: np.ndarray, target_tpr: float = 0.95) -> float:
    """Compute FPR at a requested TPR from the ROC curve."""
    y = np.asarray(y_true_unknown).astype(int)
    if y.sum() == 0 or (1 - y).sum() == 0:
        return np.nan
    fpr, tpr, _thr = roc_curve(y, scores)
    idx = np.where(tpr >= target_tpr)[0]
    if len(idx) == 0:
        return np.nan
    return float(np.min(fpr[idx]))


def _empirical_known_cdf_probability(scores: np.ndarray, known_reference_scores: np.ndarray) -> np.ndarray:
    """Map scores to a probability-like rarity value using known validation scores only."""
    ref = np.sort(np.asarray(known_reference_scores, dtype=float))
    if len(ref) == 0:
        return np.full(len(scores), np.nan)
    ranks = np.searchsorted(ref, np.asarray(scores, dtype=float), side="right")
    return np.clip(ranks / len(ref), 0.0, 1.0)


def _expected_calibration_error(y_true: np.ndarray, prob: np.ndarray, n_bins: int = 10) -> float:
    y = np.asarray(y_true).astype(float)
    p = np.asarray(prob).astype(float)
    mask = np.isfinite(p)
    y, p = y[mask], p[mask]
    if len(y) == 0:
        return np.nan
    edges = np.linspace(0.0, 1.0, n_bins + 1)
    ece = 0.0
    for lo, hi in zip(edges[:-1], edges[1:]):
        m = (p >= lo) & ((p <= hi) if hi == 1.0 else (p < hi))
        if not np.any(m):
            continue
        ece += float(m.mean()) * abs(float(np.mean(p[m])) - float(np.mean(y[m])))
    return float(ece)


def _brier_score_binary(y_true: np.ndarray, prob: np.ndarray) -> float:
    y = np.asarray(y_true).astype(float)
    p = np.asarray(prob).astype(float)
    mask = np.isfinite(p)
    if not np.any(mask):
        return np.nan
    return float(np.mean((p[mask] - y[mask]) ** 2))


def _train_open_set_rf(X_fit: pd.DataFrame, y_fit: pd.Series, seed: int) -> Pipeline:
    model = Pipeline([
        ("imputer", SimpleImputer(strategy="median")),
        ("model", RandomForestClassifier(
            n_estimators=160,
            max_depth=None,
            min_samples_leaf=2,
            class_weight="balanced_subsample",
            n_jobs=-1,
            random_state=seed,
        )),
    ])
    model.fit(X_fit, y_fit)
    return model


def _make_shifted_normal_variants(split: Dict[str, Any], config: Config) -> Dict[str, pd.DataFrame]:
    """Create normal-but-distribution-shifted healthy windows."""
    X_test = split["X_test"].reset_index(drop=True)
    y_test = split["y_test"].reset_index(drop=True)
    Xh = X_test.loc[y_test == 0].reset_index(drop=True).copy()
    if len(Xh) == 0:
        Xh = X_test.copy()
    train_scale = _training_scale(split).reindex(Xh.columns).fillna(1.0)
    rng = np.random.default_rng(config.random_seed + 2401)
    variants: Dict[str, pd.DataFrame] = {}

    X1 = Xh.copy()
    for col in X1.columns:
        if col.startswith("session__ev_requested_power_kw") or col.startswith("session__ev_delivered_power_kw"):
            X1[col] += 0.20 * train_scale[col]
        elif col.startswith("session__active_ports"):
            X1[col] += 0.15 * train_scale[col]
        elif col.startswith("grid__grid_import_kw"):
            X1[col] += 0.15 * train_scale[col]
        elif col.startswith("grid__bess_power_kw"):
            X1[col] += 0.08 * train_scale[col]
    variants["Higher normal charging demand"] = X1

    X2 = Xh.copy()
    for col in X2.columns:
        if col.startswith("thermal__ambient_temp_c"):
            X2[col] += 0.35 * train_scale[col]
        elif col.startswith("thermal__converter_temp_c"):
            X2[col] += 0.20 * train_scale[col]
        elif col.startswith("grid__pv_available_kw") or col.startswith("grid__pv_used_kw"):
            X2[col] -= 0.15 * train_scale[col]
        elif col.startswith("grid__grid_import_kw"):
            X2[col] += 0.12 * train_scale[col]
    variants["Hot/cloudy normal operation"] = X2

    X3 = Xh.copy()
    for col in X3.columns:
        if col.startswith("comm__packet_loss_rate") or col.startswith("comm__missing_data_rate"):
            X3[col] += np.maximum(0.0, rng.normal(0.0, 0.08 * train_scale[col], len(X3)))
        elif col.startswith("comm__stale_measurement_flag") or col.startswith("comm__status_inconsistency_flag"):
            X3[col] += np.maximum(0.0, rng.normal(0.0, 0.05 * train_scale[col], len(X3)))
    variants["Benign communication variability"] = X3

    X4 = Xh.copy()
    for col in X4.columns:
        if col.startswith("thermal__ambient_temp_c"):
            X4[col] += 0.25 * train_scale[col]
        elif col.startswith("session__ev_requested_power_kw") or col.startswith("session__active_ports"):
            X4[col] += 0.12 * train_scale[col]
        elif col.startswith("grid__grid_import_kw"):
            X4[col] += 0.10 * train_scale[col]
        elif col.startswith("elec__voltage_v"):
            X4[col] += rng.normal(0.0, 0.05 * train_scale[col], len(X4))
    variants["Combined shifted normal"] = X4
    return variants


def run_loco_open_set_analysis(results: Dict[str, Any], config: Config, paths: Dict[str, Path]) -> Dict[str, pd.DataFrame]:
    """Run leave-one-class-out open-set tests across all fault classes."""
