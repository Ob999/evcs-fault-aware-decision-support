def _health_state_from_risk(risk: np.ndarray, thresholds: Tuple[float, float, float]) -> Tuple[List[str], List[str]]:
    warning_t, degraded_t, critical_t = thresholds
    states, actions = [], []
    for r in np.asarray(risk, dtype=float):
        if r < warning_t:
            states.append("Healthy")
            actions.append("Normal monitoring")
        elif r < degraded_t:
            states.append("Warning")
            actions.append("Closer observation / schedule inspection")
        elif r < critical_t:
            states.append("Degraded")
            actions.append("Plan maintenance / consider derating")
        else:
            states.append("Critical")
            actions.append("Immediate intervention / isolate affected port")
    return states, actions


def _risk_components_for_matrix(
    X: pd.DataFrame,
    model: Pipeline,
    anomaly_model: Optional[Pipeline],
    anomaly_threshold: float,
    severity_map: Dict[int, float],
) -> pd.DataFrame:
    """Compute the five decision components used by the heuristic risk layer."""
    X = X.reset_index(drop=True)
    y_pred = model.predict(X)

    if hasattr(model, "predict_proba"):
        proba = model.predict_proba(X)
        confidence = proba.max(axis=1)
        classes = list(getattr(model, "classes_", []))
        if 0 in classes:
            healthy_idx = classes.index(0)
            fault_probability = 1.0 - proba[:, healthy_idx]
        else:
            fault_probability = 1.0 - proba.max(axis=1)
    else:
        confidence = np.ones(len(X)) * 0.75
        fault_probability = (y_pred != 0).astype(float)

    if anomaly_model is not None:
        anomaly_score = -anomaly_model.decision_function(X)
        anomaly_pred = (anomaly_score > anomaly_threshold).astype(int)
    else:
        anomaly_score = np.zeros(len(X))
        anomaly_pred = np.zeros(len(X), dtype=int)

    severity = np.array([severity_map.get(int(c), 0.5) for c in y_pred], dtype=float)
    persistence = pd.Series(anomaly_pred).rolling(window=4, min_periods=1).mean().values
    impact_raw = (
        0.35 * _normalize_series(X.get("session__unmet_energy_kwh_window", pd.Series(np.zeros(len(X)))).values)
        + 0.25 * _normalize_series(1.0 - X.get("session__port_available_ratio__mean", pd.Series(np.ones(len(X)))).values)
        + 0.20 * _normalize_series(X.get("grid__transformer_loading_pct__mean", pd.Series(np.zeros(len(X)))).values)
        + 0.20 * _normalize_series(X.get("comm__data_quality_risk", pd.Series(np.zeros(len(X)))).values)
    )
    impact = np.clip(impact_raw, 0, 1)
    anomaly_norm = _normalize_series(anomaly_score)

    return pd.DataFrame({
        "predicted_label": y_pred.astype(int),
        "severity": severity,
        "confidence": np.asarray(confidence, dtype=float),
        "persistence": persistence,
        "impact": impact,
        "anomaly_norm": anomaly_norm,
        "anomaly_score": anomaly_score,
        "anomaly_pred": anomaly_pred.astype(int),
        "fault_probability": np.asarray(fault_probability, dtype=float),
    })


def _build_decision_dataframe(
    meta: pd.DataFrame,
    y_true: pd.Series,
    components: pd.DataFrame,
    weights: Optional[Dict[str, float]],
    thresholds: Tuple[float, float, float],
    risk_override: Optional[np.ndarray] = None,
) -> pd.DataFrame:
    """Construct maintenance-decision rows for a given risk design."""
    meta = meta.reset_index(drop=True).copy()
    y_true = y_true.reset_index(drop=True)
    components = components.reset_index(drop=True)

    if risk_override is None:
        w = weights or {"severity": 0.35, "confidence": 0.20, "persistence": 0.20, "impact": 0.15, "anomaly_norm": 0.10}
        total = sum(max(float(v), 0.0) for v in w.values())
        if total <= 0:
            w = {k: 1.0 / 5.0 for k in ["severity", "confidence", "persistence", "impact", "anomaly_norm"]}
        else:
            w = {k: max(float(v), 0.0) / total for k, v in w.items()}
        risk = (
            w.get("severity", 0.0) * components["severity"].values
            + w.get("confidence", 0.0) * components["confidence"].values
            + w.get("persistence", 0.0) * components["persistence"].values
            + w.get("impact", 0.0) * components["impact"].values
            + w.get("anomaly_norm", 0.0) * components["anomaly_norm"].values
        )
    else:
        risk = np.asarray(risk_override, dtype=float)

    # Healthy predictions should not become high risk only because confidence is high.
    risk = np.where(
        components["predicted_label"].values == 0,
        np.minimum(risk, 0.24 + 0.20 * components["anomaly_pred"].values),
        risk,
    )
    risk = np.clip(risk, 0, 1)
    states, actions = _health_state_from_risk(risk, thresholds)

    out = meta.copy()
    out["true_label"] = y_true.values
    out["true_label_name"] = [CLASS_NAMES[int(v)] for v in y_true.values]
    out["predicted_label"] = components["predicted_label"].values
    out["predicted_label_name"] = [CLASS_NAMES[int(v)] for v in components["predicted_label"].values]
    out["diagnostic_confidence"] = components["confidence"].values
    out["anomaly_score"] = components["anomaly_score"].values
    out["anomaly_pred"] = components["anomaly_pred"].values
    out["severity_component"] = components["severity"].values
    out["persistence_component"] = components["persistence"].values
    out["operational_impact_component"] = components["impact"].values
    out["risk_score"] = risk
    out["health_state"] = states
    out["maintenance_action"] = actions
    return out


def _operational_metrics_for_decisions(
    df: pd.DataFrame,
    split: Dict[str, Any],
    decisions: pd.DataFrame,
    config: Config,
    policy_name: str,
) -> Dict[str, Any]:
    """Evaluate the scenario-based operational impact without writing files."""
    test_start_step = int(config.test_fraction_start * len(df))
    df_test = df.loc[df["step"] >= test_start_step].copy()
    dt_h = config.dt_minutes / 60.0

    total_hours = len(df_test) * dt_h
    reactive_unavailable_hours = float(np.sum(1.0 - df_test["port_available_ratio"].values) * dt_h)
    reactive_unmet_energy = float(np.sum(df_test["unmet_power_kw"].values) * dt_h)
    reactive_availability = float(1.0 - reactive_unavailable_hours / max(total_hours, 1e-9))

    predictive_unavailable_hours = reactive_unavailable_hours
    predictive_unmet_energy = reactive_unmet_energy

    test_events = df_test.loc[df_test["event_id"] >= 0, "event_id"].unique()
    detected_events = 0
    delays = []
    for eid in test_events:
        event_df = df_test.loc[df_test["event_id"] == eid]
        if event_df.empty:
            continue
        event_start_time = event_df.index.min()
        event_end_time = event_df.index.max()
        md_event = decisions.loc[decisions["dominant_event_id"] == eid].copy()
        if md_event.empty:
            continue
        action_mask = md_event["health_state"].isin(["Warning", "Degraded", "Critical"])
        if not action_mask.any():
            continue
        detection_time = pd.to_datetime(md_event.loc[action_mask, "window_end"]).min()
        if pd.isna(detection_time):
            continue
        detected_events += 1
        delay_h = max((detection_time - event_start_time).total_seconds() / 3600.0, 0.0)
        delays.append(delay_h)
        event_unavailable_h = float(np.sum(1.0 - event_df["port_available_ratio"].values) * dt_h)
        event_unmet_kwh = float(np.sum(event_df["unmet_power_kw"].values) * dt_h)
        if detection_time < event_end_time:
            remaining_fraction = max((event_end_time - detection_time).total_seconds(), 0) / max((event_end_time - event_start_time).total_seconds(), 1)
            predictive_unavailable_hours -= config.predictive_remaining_downtime_reduction * event_unavailable_h * remaining_fraction
            predictive_unmet_energy -= config.predictive_remaining_unmet_energy_reduction * event_unmet_kwh * remaining_fraction

    predictive_unavailable_hours = max(predictive_unavailable_hours, 0.0)
    predictive_unmet_energy = max(predictive_unmet_energy, 0.0)
    predictive_availability = float(1.0 - predictive_unavailable_hours / max(total_hours, 1e-9))

    action_pos = decisions["health_state"].isin(["Warning", "Degraded", "Critical"]).values
    true_fault = (decisions["true_label"].values != 0)
    tp = int(np.sum(action_pos & true_fault))
    fp = int(np.sum(action_pos & ~true_fault))
    fn = int(np.sum(~action_pos & true_fault))
    tn = int(np.sum(~action_pos & ~true_fault))

    return {
        "policy_or_scenario": policy_name,
        "port_availability_improvement_pp": 100.0 * (predictive_availability - reactive_availability),
        "unavailable_port_hour_reduction_pct": _pct_reduction(reactive_unavailable_hours, predictive_unavailable_hours),
        "unmet_energy_reduction_pct": _pct_reduction(reactive_unmet_energy, predictive_unmet_energy),
        "risk_triggered_windows": int(np.sum(action_pos)),
        "action_precision": safe_divide(tp, tp + fp),
        "action_recall": safe_divide(tp, tp + fn),
        "action_false_positive_rate": safe_divide(fp, fp + tn),
        "event_detection_rate": safe_divide(detected_events, len(test_events)),
        "mean_detection_delay_min": float(np.nanmean(delays) * 60.0) if len(delays) else np.nan,
        "mean_risk_score": float(np.mean(decisions["risk_score"].values)),
    }


def _evaluate_policy_from_components(
    df: pd.DataFrame,
    split: Dict[str, Any],
    components: pd.DataFrame,
    config: Config,
    name: str,
    weights: Optional[Dict[str, float]] = None,
    thresholds: Tuple[float, float, float] = (0.25, 0.45, 0.70),
    risk_override: Optional[np.ndarray] = None,
) -> Dict[str, Any]:
    decisions = _build_decision_dataframe(split["meta_test"], split["y_test"], components, weights, thresholds, risk_override=risk_override)
    return _operational_metrics_for_decisions(df, split, decisions, config, name)


