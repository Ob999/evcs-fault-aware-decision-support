        for model_type, label in [("1D-CNN", "1D-CNN raw-sequence baseline"), ("GRU", "GRU raw-sequence baseline")]:
            pred, status, train_t, infer_ms = _train_torch_sequence_classifier(model_type, Xseq_fit, y_fit.values, Xseq_test, y_test.values, config)
