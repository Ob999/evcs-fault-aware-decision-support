def _detected_event_action_time(event_id: int, decisions: pd.DataFrame) -> Optional[pd.Timestamp]:
    md_event = decisions.loc[decisions["dominant_event_id"] == event_id].copy() if "dominant_event_id" in decisions.columns else pd.DataFrame()
    if md_event.empty:
        return None
    action_mask = md_event["health_state"].isin(["Warning", "Degraded", "Critical"])
    if not action_mask.any():
        return None
    return pd.to_datetime(md_event.loc[action_mask, "window_end"]).min()


def _evaluate_fault_aware_energy_policy(
    df: pd.DataFrame,
    decisions: pd.DataFrame,
    config: Config,
    policy_name: str,
    action_model: Optional[pd.DataFrame] = None,
    *,
    generic_availability_recovery: float = 0.50,
    generic_unmet_recovery: float = 0.40,
    der_coupling_scale: float = 1.0,
    delay_scale: float = 1.0,
) -> Tuple[Dict[str, Any], pd.DataFrame]:
    """Evaluate service and grid/DER outcomes for a response policy.

    If action_model is None, the policy is the generic predictive-maintenance
    response that changes unavailable hours and unmet energy but leaves DER/grid
    dispatch unchanged. If action_model is supplied, class-specific response
    assumptions also modify BESS support, PV curtailment/use, grid-import peaks,
    transformer-overload hours, and controlled derating energy.
    """
    test_start_step = int(config.test_fraction_start * len(df))
    df_test = df.loc[df["step"] >= test_start_step].copy()
    dt_h = config.dt_minutes / 60.0
    base = _base_grid_der_metrics(df_test, config)
    out = dict(base)
    out["policy_or_scenario"] = policy_name

    # Start from the reactive outcome, then subtract expected avoided service loss
    # and apply expected grid/DER changes after effective response time.
    deltas = {
        "unavailable_port_hours": 0.0,
        "unmet_energy_kwh": 0.0,
        "grid_import_kwh": 0.0,
        "peak_grid_import_kw": 0.0,
        "pv_used_kwh": 0.0,
        "pv_curtailment_kwh": 0.0,
        "bess_throughput_kwh": 0.0,
        "transformer_overload_hours": 0.0,
        "derated_charging_energy_kwh": 0.0,
    }
    class_rows: Dict[int, Dict[str, Any]] = {}
    lookup: Dict[int, Dict[str, Any]] = {}
    if action_model is not None and not action_model.empty:
        lookup = {int(r["class_id"]): r.to_dict() for _, r in action_model.iterrows()}

    test_events = df_test.loc[df_test["event_id"] >= 0, "event_id"].unique()
    effective_events = 0
    detected_events = 0
    for eid in test_events:
        event_df = df_test.loc[df_test["event_id"] == eid]
        if event_df.empty:
            continue
        event_label = int(event_df["label"].mode().iloc[0])
        detection_time = _detected_event_action_time(int(eid), decisions)
        if detection_time is None:
            continue
        detected_events += 1

        if lookup:
            action = lookup.get(event_label, {})
            delay_min = float(action.get("response_delay_min", 0.0)) * float(delay_scale)
            success = float(action.get("success_probability", 1.0))
            avail_recovery = float(action.get("availability_recovery_fraction", generic_availability_recovery)) * success
            unmet_recovery = float(action.get("unmet_energy_recovery_fraction", generic_unmet_recovery)) * success
            bess_support_kw = float(action.get("bess_support_kw", 0.0)) * float(der_coupling_scale) * success
            pv_realloc_fraction = float(action.get("pv_reallocation_fraction", 0.0)) * float(der_coupling_scale) * success
            grid_peak_cut = float(action.get("grid_peak_reduction_fraction", 0.0)) * float(der_coupling_scale) * success
            overload_cut = float(action.get("transformer_overload_reduction_fraction", 0.0)) * float(der_coupling_scale) * success
            derating_fraction = float(action.get("derating_fraction", 0.0)) * float(der_coupling_scale) * success
            response_mode = str(action.get("response_mode", "Fault-aware action"))
        else:
            delay_min = 0.0
            avail_recovery = float(generic_availability_recovery)
            unmet_recovery = float(generic_unmet_recovery)
            bess_support_kw = 0.0
            pv_realloc_fraction = 0.0
            grid_peak_cut = 0.0
            overload_cut = 0.0
            derating_fraction = 0.0
            response_mode = "Generic predictive-maintenance response"

        effective_time = detection_time + pd.Timedelta(minutes=delay_min)
        if effective_time > event_df.index.max():
            continue
        effective_events += 1
        affected = event_df.loc[event_df.index >= effective_time].copy()
        if affected.empty:
            continue
        affected_h = len(affected) * dt_h
        aff_unavailable_h = float(np.sum(1.0 - affected["port_available_ratio"].values) * dt_h)
        aff_unmet_kwh = float(np.sum(affected["unmet_power_kw"].values) * dt_h)
        aff_grid_kwh = float(np.sum(affected["grid_import_kw"].values) * dt_h)
        aff_peak_grid_kw = float(np.nanmax(affected["grid_import_kw"].values)) if len(affected) else 0.0
        aff_peak_excess_kwh = float(np.sum(np.maximum(affected["grid_import_kw"].values - 0.75 * config.station_power_kw, 0.0)) * dt_h)
        aff_pv_curtail_kwh = float(np.sum(np.maximum(affected["pv_available_kw"].values - affected["pv_used_kw"].values, 0.0)) * dt_h)
        aff_overload_h = float(np.sum(affected["transformer_loading_pct"].values > 100.0) * dt_h)
        aff_delivered_kwh = float(np.sum(affected["ev_delivered_power_kw"].values) * dt_h)

        delta_unavail = min(aff_unavailable_h, avail_recovery * aff_unavailable_h)
        base_unmet_delta = min(aff_unmet_kwh, unmet_recovery * aff_unmet_kwh)
        raw_bess_support_kwh = max(bess_support_kw * affected_h, 0.0)
        bess_to_unmet = min(0.55 * raw_bess_support_kwh, max(aff_unmet_kwh - base_unmet_delta, 0.0))
        bess_to_grid = max(raw_bess_support_kwh - bess_to_unmet, 0.0)
        pv_reallocated_kwh = min(aff_pv_curtail_kwh, pv_realloc_fraction * aff_pv_curtail_kwh)
        pv_to_unmet = min(0.65 * pv_reallocated_kwh, max(aff_unmet_kwh - base_unmet_delta - bess_to_unmet, 0.0))
        delta_unmet = min(aff_unmet_kwh, base_unmet_delta + bess_to_unmet + pv_to_unmet)
        delta_grid_energy = min(
            aff_grid_kwh,
            0.80 * bess_to_grid + grid_peak_cut * aff_peak_excess_kwh + 0.10 * derating_fraction * aff_delivered_kwh,
        )
        peak_reduction_kw = min(
            aff_peak_grid_kw,
            grid_peak_cut * max(aff_peak_grid_kw - 0.65 * config.station_power_kw, 0.0) + 0.10 * bess_support_kw,
        )
        delta_overload_h = min(aff_overload_h, overload_cut * aff_overload_h + 0.10 * delta_grid_energy / max(config.station_power_kw, 1.0))
        derated_energy_kwh = derating_fraction * aff_delivered_kwh

        deltas["unavailable_port_hours"] += delta_unavail
        deltas["unmet_energy_kwh"] += delta_unmet
        deltas["grid_import_kwh"] += delta_grid_energy
        deltas["peak_grid_import_kw"] = max(deltas["peak_grid_import_kw"], peak_reduction_kw)
        deltas["pv_used_kwh"] += pv_reallocated_kwh
        deltas["pv_curtailment_kwh"] += pv_reallocated_kwh
        deltas["bess_throughput_kwh"] += raw_bess_support_kwh
        deltas["transformer_overload_hours"] += delta_overload_h
        deltas["derated_charging_energy_kwh"] += derated_energy_kwh

        if event_label not in class_rows:
            class_rows[event_label] = {
                "fault_class": CLASS_NAMES.get(event_label, str(event_label)),
                "response_mode": response_mode,
                "detected_events": 0,
                "effective_events": 0,
                "unavailable_hour_reduction_h": 0.0,
                "unmet_energy_reduction_kwh": 0.0,
                "grid_import_reduction_kwh": 0.0,
                "pv_reallocated_kwh": 0.0,
                "additional_bess_support_kwh": 0.0,
                "transformer_overload_reduction_h": 0.0,
                "derated_charging_energy_kwh": 0.0,
            }
        row = class_rows[event_label]
        row["detected_events"] += 1
        row["effective_events"] += 1
        row["unavailable_hour_reduction_h"] += delta_unavail
        row["unmet_energy_reduction_kwh"] += delta_unmet
        row["grid_import_reduction_kwh"] += delta_grid_energy
        row["pv_reallocated_kwh"] += pv_reallocated_kwh
        row["additional_bess_support_kwh"] += raw_bess_support_kwh
        row["transformer_overload_reduction_h"] += delta_overload_h
        row["derated_charging_energy_kwh"] += derated_energy_kwh

    # Apply aggregate deltas.
    out["unavailable_port_hours"] = max(base["unavailable_port_hours"] - deltas["unavailable_port_hours"], 0.0)
    out["unmet_energy_kwh"] = max(base["unmet_energy_kwh"] - deltas["unmet_energy_kwh"], 0.0)
    out["grid_import_kwh"] = max(base["grid_import_kwh"] - deltas["grid_import_kwh"], 0.0)
    out["peak_grid_import_kw"] = max(base["peak_grid_import_kw"] - deltas["peak_grid_import_kw"], 0.0)
    out["pv_used_kwh"] = min(base["pv_available_kwh"], base["pv_used_kwh"] + deltas["pv_used_kwh"])
    out["pv_curtailment_kwh"] = max(base["pv_curtailment_kwh"] - deltas["pv_curtailment_kwh"], 0.0)
    out["pv_utilization_ratio"] = safe_divide(out["pv_used_kwh"], base["pv_available_kwh"])
    out["bess_throughput_kwh"] = base["bess_throughput_kwh"] + deltas["bess_throughput_kwh"]
    out["additional_bess_support_kwh"] = deltas["bess_throughput_kwh"]
    out["transformer_overload_hours"] = max(base["transformer_overload_hours"] - deltas["transformer_overload_hours"], 0.0)
    out["derated_charging_energy_kwh"] = deltas["derated_charging_energy_kwh"]
    out["port_availability_proxy"] = 1.0 - out["unavailable_port_hours"] / max(base["total_hours"], 1e-9)
    out["fault_aware_energy_actions"] = int(effective_events)
    out["event_detection_rate"] = safe_divide(detected_events, len(test_events))
    out["effective_energy_action_rate"] = safe_divide(effective_events, len(test_events))

    # Derived changes relative to reactive operation.
    out["port_availability_improvement_pp"] = 100.0 * (out["port_availability_proxy"] - base["port_availability_proxy"])
    out["unavailable_port_hour_reduction_pct"] = _pct_reduction(base["unavailable_port_hours"], out["unavailable_port_hours"])
    out["unmet_energy_reduction_pct"] = _pct_reduction(base["unmet_energy_kwh"], out["unmet_energy_kwh"])
    out["grid_import_reduction_pct"] = _pct_reduction(base["grid_import_kwh"], out["grid_import_kwh"])
    out["peak_grid_import_reduction_pct"] = _pct_reduction(base["peak_grid_import_kw"], out["peak_grid_import_kw"])
    out["pv_utilization_change_pp"] = 100.0 * (out["pv_utilization_ratio"] - base["pv_utilization_ratio"])
    out["pv_curtailment_reduction_pct"] = _pct_reduction(base["pv_curtailment_kwh"], out["pv_curtailment_kwh"])
    out["bess_throughput_increase_pct"] = 100.0 * safe_divide(out["bess_throughput_kwh"] - base["bess_throughput_kwh"], base["bess_throughput_kwh"])
    out["transformer_overload_reduction_pct"] = _pct_reduction(base["transformer_overload_hours"], out["transformer_overload_hours"])
    out["der_coupling_scale"] = float(der_coupling_scale)
    out["delay_scale"] = float(delay_scale)

    return out, pd.DataFrame(list(class_rows.values()))


