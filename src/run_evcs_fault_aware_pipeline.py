def _minority_macro_f1(y_true: np.ndarray, y_pred: np.ndarray) -> float:
    """Macro-F1 over fault classes only (classes 1--6), useful for imbalance checks."""
    fault_labels = [i for i in sorted(CLASS_NAMES.keys()) if i != 0]
    _p, _r, f1, _support = precision_recall_fscore_support(
        y_true, y_pred, labels=fault_labels, average="macro", zero_division=0
    )
    return float(f1)


def _make_rf_pipeline(config: Config, *, n_estimators: int = 120, min_samples_leaf: int = 2, max_features: Optional[str] = "sqrt") -> Pipeline:
    return Pipeline([
        ("imputer", SimpleImputer(strategy="median")),
        ("model", RandomForestClassifier(
            n_estimators=int(n_estimators),
            min_samples_leaf=int(min_samples_leaf),
            max_features=max_features,
            class_weight="balanced_subsample",
            n_jobs=-1,
            random_state=config.random_seed,
        )),
    ])


def _fit_rf_and_score(split: Dict[str, Any], config: Config, *, n_estimators: int = 120, min_samples_leaf: int = 2, max_features: Optional[str] = "sqrt", columns: Optional[List[str]] = None) -> Dict[str, Any]:
    X_fit = pd.concat([split["X_train"], split["X_val"]], axis=0).reset_index(drop=True)
    y_fit = pd.concat([split["y_train"], split["y_val"]], axis=0).reset_index(drop=True)
    X_test = split["X_test"].reset_index(drop=True)
    y_test = split["y_test"].reset_index(drop=True)
    if columns is not None:
        X_fit = X_fit[columns]
        X_test = X_test[columns]
    model = _make_rf_pipeline(config, n_estimators=n_estimators, min_samples_leaf=min_samples_leaf, max_features=max_features)
    t0 = time.perf_counter()
    model.fit(X_fit, y_fit)
    train_time_s = time.perf_counter() - t0
    t1 = time.perf_counter()
    pred = model.predict(X_test)
    inference_ms = 1000.0 * (time.perf_counter() - t1) / max(len(X_test), 1)
    metrics = summarize_metrics(y_test.values, pred, labels=sorted(CLASS_NAMES.keys()))
    return {
        "model": model,
        "pred": np.asarray(pred),
        "accuracy": metrics["accuracy"],
        "macro_precision": metrics["macro_precision"],
        "macro_recall": metrics["macro_recall"],
        "macro_f1": metrics["macro_f1"],
        "minority_macro_f1": _minority_macro_f1(y_test.values, pred),
        "train_time_s": train_time_s,
        "inference_ms_per_window": inference_ms,
    }


def _events_from_timeseries(df: pd.DataFrame, config: Config) -> pd.DataFrame:
    """Return fault-event metadata, falling back to grouping event_id in the time series."""
    events_df = df.attrs.get("events_df", pd.DataFrame())
    if events_df is not None and not events_df.empty:
        return events_df.copy()
    rows = []
    ev_df = df.loc[df.get("event_id", pd.Series(index=df.index, dtype=int)) >= 0].copy()
    if ev_df.empty:
        return pd.DataFrame()
    for eid, grp in ev_df.groupby("event_id"):
        cls = int(grp["label"].mode().iloc[0]) if "label" in grp else -1
        rows.append({
            "event_id": int(eid),
            "class_id": cls,
            "class_name": CLASS_NAMES.get(cls, str(cls)),
            "start_time": grp.index.min(),
            "end_time": grp.index.max(),
            "start_step": int(grp["step"].min()) if "step" in grp else np.nan,
            "end_step": int(grp["step"].max()) if "step" in grp else np.nan,
            "segment": "test" if int(grp["step"].min()) >= int(config.test_fraction_start * len(df)) else ("validation" if int(grp["step"].min()) >= int(config.val_fraction_start * len(df)) else "training"),
        })
    return pd.DataFrame(rows)


def _event_metrics_from_window_predictions(meta_test: pd.DataFrame, y_pred: np.ndarray, df: pd.DataFrame, config: Config) -> Dict[str, float]:
    """Compute compact event detection rate and mean delay using window predictions."""
    events_df = _events_from_timeseries(df, config)
    if events_df.empty or meta_test.empty:
        return {"event_detection_rate": np.nan, "mean_detection_delay_min": np.nan, "n_test_events": 0, "missed_events": np.nan}
    ev = events_df.copy()
    if "segment" in ev.columns:
        ev = ev.loc[ev["segment"].astype(str).str.lower() == "test"].copy()
    else:
        ev = ev.loc[pd.to_numeric(ev.get("start_step", 0), errors="coerce") >= int(config.test_fraction_start * len(df))].copy()
    if ev.empty:
        return {"event_detection_rate": np.nan, "mean_detection_delay_min": np.nan, "n_test_events": 0, "missed_events": np.nan}
    md = meta_test.reset_index(drop=True).copy()
    md["pred"] = np.asarray(y_pred)
    md["window_end"] = pd.to_datetime(md["window_end"])
    detected_count = 0
    delays = []
    for _, row in ev.iterrows():
        eid = int(row["event_id"])
        start_time = pd.to_datetime(row["start_time"])
        end_time = pd.to_datetime(row["end_time"])
        event_windows = md.loc[(md["dominant_event_id"] == eid) & (md["pred"] != 0)].copy()
        if event_windows.empty:
            continue
        det_time = pd.to_datetime(event_windows["window_end"]).min()
        if det_time <= end_time:
            detected_count += 1
            delays.append(max((det_time - start_time).total_seconds() / 60.0, 0.0))
    return {
        "event_detection_rate": safe_divide(float(detected_count), float(len(ev))),
        "mean_detection_delay_min": float(np.nanmean(delays)) if len(delays) else np.nan,
        "n_test_events": int(len(ev)),
        "missed_events": int(len(ev) - detected_count),
    }


def _class_recall(y_true: np.ndarray, y_pred: np.ndarray, cls: int) -> float:
    mask = np.asarray(y_true) == cls
    if mask.sum() == 0:
        return np.nan
    return float((np.asarray(y_pred)[mask] == cls).sum() / mask.sum())


def _top_confusion_pair(y_true: np.ndarray, y_pred: np.ndarray) -> str:
    labels = sorted(CLASS_NAMES.keys())
    cm = confusion_matrix(y_true, y_pred, labels=labels)
    np.fill_diagonal(cm, 0)
    if cm.sum() == 0:
        return "None"
    i, j = np.unravel_index(np.argmax(cm), cm.shape)
    return f"{CLASS_SHORT[labels[i]]}->{CLASS_SHORT[labels[j]]} ({int(cm[i, j])})"


def _correlation_pruned_columns(X: pd.DataFrame, threshold: float = 0.90) -> Tuple[List[str], int, List[Tuple[str, str, float]]]:
    """Greedy correlation pruning using training data only."""
    X_num = X.astype(float).copy()
    corr = X_num.corr().abs().fillna(0.0)
    upper = corr.where(np.triu(np.ones(corr.shape), k=1).astype(bool))
    high_pairs = []
    for col in upper.columns:
        vals = upper[col].dropna()
        vals = vals[vals > threshold]
        for idx, val in vals.items():
            high_pairs.append((str(idx), str(col), float(val)))
    to_drop = [column for column in upper.columns if any(upper[column] > threshold)]
    kept = [c for c in X.columns if c not in set(to_drop)]
    high_pairs_sorted = sorted(high_pairs, key=lambda x: x[2], reverse=True)
    return kept, len(high_pairs), high_pairs_sorted[:5]


