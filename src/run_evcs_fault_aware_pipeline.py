def _operational_response_metrics(
    df: pd.DataFrame,
    decisions: pd.DataFrame,
    config: Config,
    policy_name: str,
    downtime_reduction: float = 0.50,
    unmet_energy_reduction: float = 0.40,
    response_delay_min: float = 0.0,
    class_action_model: Optional[pd.DataFrame] = None,
) -> Dict[str, Any]:
    """Evaluate scenario-based operational response under explicit assumptions.

    If class_action_model is supplied, event-specific response effectiveness and
    latency are selected from the table using the event's fault class. The
    effective reduction is multiplied by the remote/on-time success probability,
    which is an expected-value approximation rather than a stochastic field claim.
    """
    test_start_step = int(config.test_fraction_start * len(df))
    df_test = df.loc[df["step"] >= test_start_step].copy()
    dt_h = config.dt_minutes / 60.0

    total_hours = len(df_test) * dt_h
    reactive_unavailable_hours = float(np.sum(1.0 - df_test["port_available_ratio"].values) * dt_h)
    reactive_unmet_energy = float(np.sum(df_test["unmet_power_kw"].values) * dt_h)
    reactive_availability = float(1.0 - reactive_unavailable_hours / max(total_hours, 1e-9))

    predictive_unavailable_hours = reactive_unavailable_hours
    predictive_unmet_energy = reactive_unmet_energy

    model_lookup: Dict[int, Dict[str, Any]] = {}
    if class_action_model is not None and not class_action_model.empty:
        for _, row in class_action_model.iterrows():
            model_lookup[int(row["class_id"])] = row.to_dict()

    test_events = df_test.loc[df_test["event_id"] >= 0, "event_id"].unique()
    detected_events = 0
    effective_interventions = 0
    delays_min: List[float] = []
    effective_delays_min: List[float] = []
    event_rows = []

    for eid in test_events:
        event_df = df_test.loc[df_test["event_id"] == eid]
        if event_df.empty:
            continue
        event_start_time = event_df.index.min()
        event_end_time = event_df.index.max()
        event_label = int(event_df["label"].mode().iloc[0])

        md_event = decisions.loc[decisions["dominant_event_id"] == eid].copy()
        if md_event.empty:
            event_rows.append({
                "event_id": int(eid),
                "class_id": event_label,
                "class_name": CLASS_NAMES.get(event_label, str(event_label)),
                "detected": False,
                "effective_intervention": False,
                "detection_delay_min": np.nan,
                "effective_response_delay_min": np.nan,
            })
            continue

        action_mask = md_event["health_state"].isin(["Warning", "Degraded", "Critical"])
        if not action_mask.any():
            event_rows.append({
                "event_id": int(eid),
                "class_id": event_label,
                "class_name": CLASS_NAMES.get(event_label, str(event_label)),
                "detected": False,
                "effective_intervention": False,
                "detection_delay_min": np.nan,
                "effective_response_delay_min": np.nan,
            })
            continue

        detection_time = pd.to_datetime(md_event.loc[action_mask, "window_end"]).min()
        if pd.isna(detection_time):
            continue

        detected_events += 1
        detection_delay_min = max((detection_time - event_start_time).total_seconds() / 60.0, 0.0)
        delays_min.append(detection_delay_min)

        if model_lookup:
            action = model_lookup.get(event_label, {})
            base_down = float(action.get("downtime_reduction_assumption", downtime_reduction))
            base_unmet = float(action.get("unmet_energy_reduction_assumption", unmet_energy_reduction))
            prob = float(action.get("remote_success_probability", 1.0))
            eff_down = np.clip(base_down * prob, 0.0, 1.0)
            eff_unmet = np.clip(base_unmet * prob, 0.0, 1.0)
            delay_min = float(action.get("response_delay_min", response_delay_min))
        else:
            eff_down = np.clip(float(downtime_reduction), 0.0, 1.0)
            eff_unmet = np.clip(float(unmet_energy_reduction), 0.0, 1.0)
            delay_min = float(response_delay_min)

        effective_time = detection_time + pd.Timedelta(minutes=delay_min)
        event_unavailable_h = float(np.sum(1.0 - event_df["port_available_ratio"].values) * dt_h)
        event_unmet_kwh = float(np.sum(event_df["unmet_power_kw"].values) * dt_h)

        effective = bool(effective_time < event_end_time)
        effective_delay_min = max((effective_time - event_start_time).total_seconds() / 60.0, 0.0)
        if effective:
            effective_interventions += 1
            effective_delays_min.append(effective_delay_min)
            remaining_fraction = max((event_end_time - effective_time).total_seconds(), 0.0) / max((event_end_time - event_start_time).total_seconds(), 1.0)
            predictive_unavailable_hours -= eff_down * event_unavailable_h * remaining_fraction
            predictive_unmet_energy -= eff_unmet * event_unmet_kwh * remaining_fraction

        event_rows.append({
            "event_id": int(eid),
            "class_id": event_label,
            "class_name": CLASS_NAMES.get(event_label, str(event_label)),
            "detected": True,
            "effective_intervention": effective,
            "detection_delay_min": detection_delay_min,
            "effective_response_delay_min": effective_delay_min,
            "downtime_reduction_assumption": eff_down,
            "unmet_energy_reduction_assumption": eff_unmet,
        })

    predictive_unavailable_hours = max(predictive_unavailable_hours, 0.0)
    predictive_unmet_energy = max(predictive_unmet_energy, 0.0)
    predictive_availability = float(1.0 - predictive_unavailable_hours / max(total_hours, 1e-9))

    return {
        "policy_or_scenario": policy_name,
        "downtime_reduction_assumption_pct": _safe_pct(downtime_reduction),
        "unmet_energy_reduction_assumption_pct": _safe_pct(unmet_energy_reduction),
        "response_delay_min": float(response_delay_min),
        "port_availability_improvement_pp": 100.0 * (predictive_availability - reactive_availability),
        "unavailable_port_hour_reduction_pct": _pct_reduction(reactive_unavailable_hours, predictive_unavailable_hours),
        "unmet_energy_reduction_pct": _pct_reduction(reactive_unmet_energy, predictive_unmet_energy),
        "reactive_unavailable_port_hours": reactive_unavailable_hours,
        "predictive_unavailable_port_hours": predictive_unavailable_hours,
        "reactive_unmet_energy_kwh": reactive_unmet_energy,
        "predictive_unmet_energy_kwh": predictive_unmet_energy,
        "event_detection_rate": safe_divide(detected_events, len(test_events)),
        "effective_intervention_rate": safe_divide(effective_interventions, len(test_events)),
        "mean_detection_delay_min": float(np.nanmean(delays_min)) if len(delays_min) else np.nan,
        "mean_effective_response_delay_min": float(np.nanmean(effective_delays_min)) if len(effective_delays_min) else np.nan,
    }


