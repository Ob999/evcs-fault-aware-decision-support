def _candidate_starts_for_segment(n_steps: int, seg_start: int, seg_end: int, n_events: int, rng: np.random.Generator,
                                  min_duration: int, max_duration: int, gap: int) -> List[Tuple[int, int]]:
    """Select non-overlapping event start/end pairs within a segment."""
    events = []
    attempts = 0
    max_attempts = n_events * 200
    while len(events) < n_events and attempts < max_attempts:
        attempts += 1
        dur = int(rng.integers(min_duration, max_duration + 1))
        if seg_end - seg_start <= dur + 2 * gap:
            break
        start = int(rng.integers(seg_start + gap, seg_end - dur - gap))
        end = start + dur
        overlaps = any(not (end + gap < s or start - gap > e) for s, e in events)
        if not overlaps:
            events.append((start, end))
    events.sort()
    return events


def inject_fault_scenarios(df: pd.DataFrame, config: Config) -> pd.DataFrame:
    """Inject class-specific anomalies into the healthy EVCS time series."""
    rng = np.random.default_rng(config.random_seed + 123)
    out = df.copy()
    n_steps = len(out)

    # Ensure each split segment contains events from each class.
    train_start, train_end = 0, int(config.val_fraction_start * n_steps)
    val_start, val_end = train_end, int(config.test_fraction_start * n_steps)
    test_start, test_end = val_end, n_steps
    segments = [(train_start, train_end), (val_start, val_end), (test_start, test_end)]

    event_counter = 0
    all_events: List[Dict[str, Any]] = []

    for cls in range(1, 7):
        for seg_id, (s0, s1) in enumerate(segments):
            events = _candidate_starts_for_segment(
                n_steps, s0, s1, config.n_events_per_class_per_segment, rng,
                config.min_event_duration_steps, config.max_event_duration_steps,
                config.event_gap_steps,
            )
            for start, end in events:
                # Avoid overwriting pre-existing faults. If overlap, skip.
                idx = out.index[start:end]
                if (out.loc[idx, "label"] != 0).any():
                    continue
                eid = event_counter
                event_counter += 1
                out.loc[idx, "label"] = cls
                out.loc[idx, "is_anomaly"] = 1
                out.loc[idx, "event_id"] = eid
                out.loc[idx, "event_phase"] = CLASS_SHORT[cls]

                all_events.append({
                    "event_id": eid,
                    "class_id": cls,
                    "class_name": CLASS_NAMES[cls],
                    "segment": ["train", "validation", "test"][seg_id],
                    "start_step": start,
                    "end_step": end,
                    "duration_steps": end - start,
                    "duration_hours": (end - start) * config.dt_minutes / 60.0,
                    "start_time": str(out.index[start]),
                    "end_time": str(out.index[end - 1]),
                })

                # Apply scenario signatures.
                n = len(idx)
                ramp = np.linspace(0.2, 1.0, n)
                noise = lambda scale: rng.normal(0, scale, n)

                if cls == 1:  # Converter/open-circuit fault
                    out.loc[idx, "thd_i"] += 0.09 * ramp + noise(0.006)
                    out.loc[idx, "current_imbalance"] += 0.06 * ramp + noise(0.004)
                    out.loc[idx, "ev_delivered_power_kw"] *= (0.88 - 0.12 * ramp)
                    out.loc[idx, "unmet_power_kw"] += out.loc[idx, "ev_requested_power_kw"].values * (0.08 + 0.08 * ramp)
                    out.loc[idx, "converter_temp_c"] += 4.0 * ramp + noise(0.4)
                    out.loc[idx, "power_balance_residual_kw"] += 12 * ramp + noise(1.2)

                elif cls == 2:  # Sensor/measurement fault
                    bias_current = rng.choice([-1, 1]) * rng.uniform(20, 55)
                    bias_voltage = rng.choice([-1, 1]) * rng.uniform(6, 16)
                    drift = np.linspace(0, rng.choice([-1, 1]) * rng.uniform(10, 25), n)
                    out.loc[idx, "current_a"] += bias_current + drift + noise(3.0)
                    out.loc[idx, "voltage_v"] += bias_voltage + 0.2 * drift + noise(0.8)
                    out.loc[idx, "power_balance_residual_kw"] += rng.choice([-1, 1]) * (8 + 8 * ramp) + noise(1.0)
                    out.loc[idx, "missing_data_rate"] += 0.025 * (rng.random(n) < 0.35)

                elif cls == 3:  # Thermal/cooling anomaly
                    out.loc[idx, "converter_temp_c"] += 10 + 22 * ramp + noise(0.7)
                    out.loc[idx, "thermal_residual_c"] += 6 + 16 * ramp + noise(0.5)
                    out.loc[idx, "thd_i"] += 0.015 * ramp + noise(0.003)

                elif cls == 4:  # Port/session failure
                    out.loc[idx, "port_available_ratio"] = np.maximum(0.25, 1.0 - 0.22 * np.ceil(1 + 3 * ramp))
                    out.loc[idx, "failed_session_flag"] = (rng.random(n) < (0.45 + 0.25 * ramp)).astype(int)
                    out.loc[idx, "interrupted_session_flag"] = (rng.random(n) < (0.25 + 0.25 * ramp)).astype(int)
                    out.loc[idx, "ev_delivered_power_kw"] *= np.maximum(0.45, 1.0 - 0.45 * ramp)
                    out.loc[idx, "unmet_power_kw"] += out.loc[idx, "ev_requested_power_kw"].values * (0.20 + 0.25 * ramp)

                elif cls == 5:  # Communication/data anomaly
                    out.loc[idx, "packet_loss_rate"] += 0.18 + 0.35 * ramp + noise(0.02)
                    out.loc[idx, "missing_data_rate"] += 0.10 + 0.25 * ramp + noise(0.01)
                    out.loc[idx, "status_inconsistency_flag"] = (rng.random(n) < (0.30 + 0.45 * ramp)).astype(int)
                    out.loc[idx, "stale_measurement_flag"] = (rng.random(n) < (0.25 + 0.45 * ramp)).astype(int)
                    # Spoofed/stale measurement proxy.
                    stale_blocks = rng.random(n) < 0.2
                    out.loc[idx, "current_a"] += stale_blocks * rng.normal(35, 10, n)
                    out.loc[idx, "power_balance_residual_kw"] += stale_blocks * rng.normal(10, 4, n)

                elif cls == 6:  # Grid-side disturbance
                    out.loc[idx, "voltage_v"] -= 18 * ramp + noise(1.0)
                    out.loc[idx, "grid_import_kw"] += 45 * ramp + noise(5)
                    out.loc[idx, "transformer_loading_pct"] += 24 * ramp + noise(2.5)
                    out.loc[idx, "voltage_deviation_pct"] = np.abs(out.loc[idx, "voltage_v"] - config.nominal_voltage_v) / config.nominal_voltage_v * 100
                    out.loc[idx, "bess_discharge_kw"] = np.minimum(config.bess_max_discharge_kw, out.loc[idx, "bess_discharge_kw"] + 30 * ramp)
                    out.loc[idx, "bess_soc"] = np.maximum(config.bess_min_soc, out.loc[idx, "bess_soc"] - 0.015 * ramp)
                    out.loc[idx, "thd_i"] += 0.035 * ramp + noise(0.004)

    # Post-process plausible ranges.
    out["thd_i"] = out["thd_i"].clip(0, 0.60)
    out["current_imbalance"] = out["current_imbalance"].clip(0, 0.40)
    out["port_available_ratio"] = out["port_available_ratio"].clip(0, 1)
    out["packet_loss_rate"] = out["packet_loss_rate"].clip(0, 1)
    out["missing_data_rate"] = out["missing_data_rate"].clip(0, 1)
    out["transformer_loading_pct"] = out["transformer_loading_pct"].clip(0, 180)
    out["ev_delivered_power_kw"] = out["ev_delivered_power_kw"].clip(lower=0)
    out["unmet_power_kw"] = out["unmet_power_kw"].clip(lower=0)

    events_df = pd.DataFrame(all_events)
    out.attrs["events_df"] = events_df
    return out


