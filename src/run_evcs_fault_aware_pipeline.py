def _base_grid_der_metrics(df_test: pd.DataFrame, config: Config) -> Dict[str, float]:
    """Compute baseline grid/DER and service metrics for a held-out test period."""
    dt_h = config.dt_minutes / 60.0
    total_hours = len(df_test) * dt_h
    unavailable_hours = float(np.sum(1.0 - df_test["port_available_ratio"].values) * dt_h)
    unmet_energy = float(np.sum(df_test["unmet_power_kw"].values) * dt_h)
    grid_import = float(np.sum(df_test["grid_import_kw"].values) * dt_h)
    peak_grid = float(np.nanmax(df_test["grid_import_kw"].values)) if len(df_test) else 0.0
    pv_available = float(np.sum(df_test["pv_available_kw"].values) * dt_h)
    pv_used = float(np.sum(df_test["pv_used_kw"].values) * dt_h)
    pv_curtail = float(np.sum(np.maximum(df_test["pv_available_kw"].values - df_test["pv_used_kw"].values, 0.0)) * dt_h)
    bess_throughput = float(np.sum(np.abs(df_test["bess_power_kw"].values)) * dt_h)
    bess_discharge = float(np.sum(np.maximum(df_test["bess_power_kw"].values, 0.0)) * dt_h)
    transformer_overload_h = float(np.sum(df_test["transformer_loading_pct"].values > 100.0) * dt_h)
    delivered_energy = float(np.sum(df_test["ev_delivered_power_kw"].values) * dt_h)
    return {
        "total_hours": total_hours,
        "port_availability_proxy": 1.0 - unavailable_hours / max(total_hours, 1e-9),
        "unavailable_port_hours": unavailable_hours,
        "unmet_energy_kwh": unmet_energy,
        "grid_import_kwh": grid_import,
        "peak_grid_import_kw": peak_grid,
        "pv_available_kwh": pv_available,
        "pv_used_kwh": pv_used,
        "pv_curtailment_kwh": pv_curtail,
        "pv_utilization_ratio": safe_divide(pv_used, pv_available),
        "bess_throughput_kwh": bess_throughput,
        "bess_discharge_kwh": bess_discharge,
        "transformer_overload_hours": transformer_overload_h,
        "delivered_energy_kwh": delivered_energy,
        "additional_bess_support_kwh": 0.0,
        "derated_charging_energy_kwh": 0.0,
        "fault_aware_energy_actions": 0,
    }


