def _model_available_note(import_name: str) -> Tuple[bool, str]:
    try:
        __import__(import_name)
        return True, "available"
    except Exception as exc:
        return False, f"skipped: {type(exc).__name__}"


def _fit_predict_tabular_model(name: str, model: Any, X_fit: pd.DataFrame, y_fit: pd.Series, X_test: pd.DataFrame, y_test: pd.Series, labels: List[int], sample_weight: Optional[np.ndarray] = None) -> Dict[str, Any]:
    t0 = time.perf_counter()
    try:
        if sample_weight is not None:
            # Pipelines need the step name for sample_weight; plain estimators accept it directly.
            if isinstance(model, Pipeline):
                last_step = model.steps[-1][0]
                model.fit(X_fit, y_fit, **{f"{last_step}__sample_weight": sample_weight})
            else:
                model.fit(X_fit, y_fit, sample_weight=sample_weight)
        else:
            model.fit(X_fit, y_fit)
        train_time = time.perf_counter() - t0
        t1 = time.perf_counter()
        pred = model.predict(X_test)
        infer_ms = 1000.0 * (time.perf_counter() - t1) / max(len(X_test), 1)
        out = {"model": name, **summarize_metrics(y_test.values, pred, labels=labels), "train_time_s": train_time, "inference_ms_per_window": infer_ms, "status": "ok"}
        return out
    except Exception as exc:
        return {"model": name, "accuracy": np.nan, "macro_precision": np.nan, "macro_recall": np.nan, "macro_f1": np.nan, "weighted_f1": np.nan, "train_time_s": np.nan, "inference_ms_per_window": np.nan, "status": f"skipped/error: {type(exc).__name__}: {str(exc)[:120]}"}


def _balanced_sample_weights(y: pd.Series) -> np.ndarray:
    classes = np.array(sorted(pd.unique(y)))
    weights = compute_class_weight(class_weight="balanced", classes=classes, y=y.values)
    weight_map = {int(c): float(w) for c, w in zip(classes, weights)}
    return np.array([weight_map[int(v)] for v in y.values], dtype=float)


