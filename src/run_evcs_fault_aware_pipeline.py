def _healthy_reference(split: Dict[str, Any]) -> pd.Series:
    """Median healthy training feature vector used as the local normal reference."""
    X_train = split["X_train"]
    y_train = split["y_train"]
    if (y_train == 0).sum() == 0:
        return X_train.median(numeric_only=True)
    return X_train.loc[y_train == 0].median(numeric_only=True)


def _training_scale(split: Dict[str, Any]) -> pd.Series:
    scale = split["X_train"].std(numeric_only=True).replace(0, 1.0)
    return scale.reindex(split["X_train"].columns).fillna(1.0)


def _scale_fault_rows_toward_healthy(
    X: pd.DataFrame,
    y: pd.Series,
    healthy_ref: pd.Series,
    scale_factor: float,
    rng: Optional[np.random.Generator] = None,
    noise_fraction: float = 0.0,
    train_scale: Optional[pd.Series] = None,
) -> pd.DataFrame:
    """Shrink non-healthy rows toward the healthy median to simulate milder faults."""
    X_out = X.copy()
    mask = (y.values != 0)
    ref = healthy_ref.reindex(X.columns).astype(float)
    X_out.loc[mask, :] = ref.values + scale_factor * (X.loc[mask, :].values - ref.values)
    if rng is not None and noise_fraction > 0:
        if train_scale is None:
            train_scale = X.std(numeric_only=True).replace(0, 1.0)
        sc = train_scale.reindex(X.columns).fillna(1.0).values
        X_out.loc[mask, :] = X_out.loc[mask, :].values + rng.normal(0, noise_fraction, size=(mask.sum(), X.shape[1])) * sc
    return X_out


def _make_overlapping_fault_variant(split: Dict[str, Any], config: Config) -> pd.DataFrame:
    """Create a harder test set by reducing amplitudes and mixing weak secondary class shifts."""
    rng = np.random.default_rng(config.random_seed + 1601)
    X = split["X_test"].reset_index(drop=True)
    y = split["y_test"].reset_index(drop=True)
    ref = _healthy_reference(split).reindex(X.columns)
    train_scale = _training_scale(split).reindex(X.columns)
    X_hard = _scale_fault_rows_toward_healthy(X, y, ref, 0.55, rng, noise_fraction=0.025, train_scale=train_scale)

    # Add weak secondary signatures from another fault class. This deliberately
    # creates feature overlap without changing the original single-label target.
    class_medians: Dict[int, pd.Series] = {}
    X_train_all = pd.concat([split["X_train"], split["X_val"]], axis=0).reset_index(drop=True)
    y_train_all = pd.concat([split["y_train"], split["y_val"]], axis=0).reset_index(drop=True)
    for cls in sorted(CLASS_NAMES.keys()):
        if cls == 0:
            continue
        if (y_train_all == cls).sum() >= 3:
            class_medians[cls] = X_train_all.loc[y_train_all == cls].median(numeric_only=True).reindex(X.columns)
    available_classes = sorted(class_medians.keys())
    for i, yi in enumerate(y.values):
        if yi == 0 or len(available_classes) < 2:
            continue
        other_choices = [c for c in available_classes if c != int(yi)]
        other = int(rng.choice(other_choices))
        secondary_delta = (class_medians[other] - ref).values
        X_hard.iloc[i, :] = X_hard.iloc[i, :].values + 0.18 * secondary_delta
    return X_hard


def _make_incipient_drift_variant(split: Dict[str, Any], config: Config) -> pd.DataFrame:
    """Create incipient drifting faults by ramping each event from weak to moderate amplitude."""
    X = split["X_test"].reset_index(drop=True)
    y = split["y_test"].reset_index(drop=True)
    meta = split["meta_test"].reset_index(drop=True)
    ref = _healthy_reference(split).reindex(X.columns)
    X_out = X.copy()
    fault_mask = y.values != 0
    X_out.loc[fault_mask, :] = ref.values

    # Per-event ramp. If no event ID is available, fall back to chronological ramp.
    for eid, idx in meta.loc[fault_mask].groupby("dominant_event_id").groups.items():
        idx_list = list(idx)
        if len(idx_list) == 0:
            continue
        ramp = np.linspace(0.15, 0.65, len(idx_list))
        for j, row_idx in enumerate(idx_list):
            X_out.iloc[row_idx, :] = ref.values + ramp[j] * (X.iloc[row_idx, :].values - ref.values)
    return X_out


def _make_nonstationary_variant(split: Dict[str, Any], config: Config) -> pd.DataFrame:
    """Add realistic nonstationary operating shifts to all test windows."""
    rng = np.random.default_rng(config.random_seed + 1703)
    X = split["X_test"].reset_index(drop=True).copy()
    y = split["y_test"].reset_index(drop=True)
    train_scale = _training_scale(split).reindex(X.columns).fillna(1.0)
    n = len(X)
    trend = np.linspace(0, 1, n)

    for col in X.columns:
        if col.startswith("thermal__ambient_temp_c") or col.startswith("thermal__converter_temp_c"):
            X[col] += 2.5 + 2.5 * trend
        elif col.startswith("grid__grid_import_kw") or col.startswith("grid__transformer_loading_pct"):
            X[col] += 0.20 * train_scale[col] * (0.5 + trend)
        elif col.startswith("session__ev_requested_power_kw") or col.startswith("session__active_ports"):
            X[col] += 0.15 * train_scale[col] * np.sin(np.linspace(0, 3 * np.pi, n))
        elif col.startswith("elec__voltage_v"):
            X[col] += rng.normal(0, 0.10 * train_scale[col], n)
        elif col.startswith("comm__packet_loss_rate") or col.startswith("comm__missing_data_rate"):
            X[col] += np.maximum(0, rng.normal(0.01, 0.01, n))
    # Combine nonstationarity with slightly milder faults to avoid an easy shifted copy.
    ref = _healthy_reference(split).reindex(X.columns)
    X = _scale_fault_rows_toward_healthy(X, y, ref, 0.75, rng, noise_fraction=0.015, train_scale=train_scale)
    return X


def _evaluate_known_fault_variant(
    variant_name: str,
    X_eval: pd.DataFrame,
    y_eval: pd.Series,
    supervised_model: Pipeline,
    anomaly_model: Pipeline,
    anomaly_threshold: float,
) -> Dict[str, Any]:
    labels = sorted(CLASS_NAMES.keys())
    pred = supervised_model.predict(X_eval)
    metrics = summarize_metrics(y_eval.values, pred, labels=labels)
    y_bin = (y_eval.values != 0).astype(int)
    score = -anomaly_model.decision_function(X_eval)
    pred_anom = (score > anomaly_threshold).astype(int)
    try:
        roc = float(roc_auc_score(y_bin, score))
    except ValueError:
        roc = np.nan
    try:
        pr = float(average_precision_score(y_bin, score))
    except ValueError:
        pr = np.nan
    return {
        "condition": variant_name,
        "n_windows": int(len(X_eval)),
        "n_fault_windows": int(y_bin.sum()),
        **metrics,
        "anomaly_roc_auc": roc,
        "anomaly_pr_auc": pr,
        "anomaly_recall_at_threshold": float(recall_score(y_bin, pred_anom, zero_division=0)),
        "anomaly_precision_at_threshold": float(precision_score(y_bin, pred_anom, zero_division=0)),
    }


def _make_mixed_fault_features(
    split: Dict[str, Any],
    class_a: int,
    class_b: int,
    n_samples: int,
    config: Config,
) -> pd.DataFrame:
    """Generate feature-level compound faults by combining two class-shift vectors."""
    rng = np.random.default_rng(config.random_seed + 1800 + 10 * class_a + class_b)
    X_train = pd.concat([split["X_train"], split["X_val"]], axis=0).reset_index(drop=True)
    y_train = pd.concat([split["y_train"], split["y_val"]], axis=0).reset_index(drop=True)
    X_healthy = X_train.loc[y_train == 0].reset_index(drop=True)
    if len(X_healthy) == 0:
        X_healthy = X_train.copy()
    ref = X_healthy.median(numeric_only=True).reindex(X_train.columns)
    median_a = X_train.loc[y_train == class_a].median(numeric_only=True).reindex(X_train.columns)
    median_b = X_train.loc[y_train == class_b].median(numeric_only=True).reindex(X_train.columns)
    delta_a = (median_a - ref).fillna(0.0).values
    delta_b = (median_b - ref).fillna(0.0).values
    base_idx = rng.choice(len(X_healthy), size=n_samples, replace=True)
    X_base = X_healthy.iloc[base_idx].reset_index(drop=True).copy()
    scale = X_train.std(numeric_only=True).replace(0, 1.0).reindex(X_train.columns).fillna(1.0).values
    # Mixed/compound events are intentionally not exact sums: use partial signatures
    # and small noise so they test open-set behaviour rather than class memorization.
    X_mix = X_base.values + 0.50 * delta_a + 0.50 * delta_b + rng.normal(0, 0.02, size=X_base.shape) * scale
    return pd.DataFrame(X_mix, columns=X_train.columns)


def _evaluate_mixed_fault_detection(
    name: str,
    X_mix: pd.DataFrame,
    supervised_model: Pipeline,
    anomaly_model: Pipeline,
    anomaly_threshold: float,
) -> Dict[str, Any]:
    pred = supervised_model.predict(X_mix)
    nonhealthy_rate = float((pred != 0).mean())
    score = -anomaly_model.decision_function(X_mix)
    anomaly_pred = (score > anomaly_threshold).astype(int)
    return {
        "mixed_fault_case": name,
        "n_windows": int(len(X_mix)),
        "rf_nonhealthy_prediction_rate": nonhealthy_rate,
        "anomaly_detection_rate": float(anomaly_pred.mean()),
        "mean_anomaly_score": float(np.mean(score)),
        "median_anomaly_score": float(np.median(score)),
        "most_common_rf_prediction": CLASS_NAMES[int(pd.Series(pred).mode().iloc[0])] if len(pred) else "N/A",
    }


def _feature_iqr_overlap(X: pd.DataFrame, y: pd.Series, max_features: int = 30) -> float:
    """Average pairwise IQR overlap across a subset of high-variance features."""
    # Use the highest-variance features to keep the diagnostic meaningful and efficient.
    variances = X.var(numeric_only=True).sort_values(ascending=False)
    cols = variances.head(min(max_features, len(variances))).index.tolist()
    classes = [c for c in sorted(np.unique(y.values)) if (y.values == c).sum() >= 3]
    if len(classes) < 2 or len(cols) == 0:
        return np.nan
    overlaps = []
    for col in cols:
        intervals = {}
        for c in classes:
            vals = X.loc[y.values == c, col].astype(float).values
            q1, q3 = np.nanpercentile(vals, [25, 75])
            intervals[c] = (q1, q3)
        for i, c1 in enumerate(classes):
            for c2 in classes[i + 1:]:
                a1, a2 = intervals[c1]
                b1, b2 = intervals[c2]
                union = max(a2, b2) - min(a1, b1)
                inter = max(0.0, min(a2, b2) - max(a1, b1))
                overlaps.append(inter / (union + 1e-12))
    return float(np.nanmean(overlaps)) if overlaps else np.nan


def _separability_diagnostics(condition: str, X: pd.DataFrame, y: pd.Series, config: Config) -> Dict[str, Any]:
    """Compute pre-training class-separability diagnostics in standardized feature space."""
    # Remove classes with fewer than 3 samples for stability.
    counts = y.value_counts()
    keep_classes = sorted([int(c) for c, n in counts.items() if n >= 3])
    keep_mask = y.isin(keep_classes).values
    Xk = X.loc[keep_mask].reset_index(drop=True)
    yk = y.loc[keep_mask].reset_index(drop=True)
    if len(keep_classes) < 2 or len(Xk) < 10:
        return {
            "condition": condition,
            "n_windows": int(len(X)),
            "n_classes": int(len(keep_classes)),
            "centroid_silhouette_proxy": np.nan,
            "mean_inter_centroid_distance": np.nan,
            "min_inter_centroid_distance": np.nan,
            "mean_nearest_centroid_margin": np.nan,
            "mean_feature_iqr_overlap": np.nan,
        }
    scaler = StandardScaler()
    Xs = scaler.fit_transform(Xk.values)
    centroids = []
    for c in keep_classes:
        centroids.append(Xs[yk.values == c].mean(axis=0))
    centroids = np.vstack(centroids)
    D = pairwise_distances(centroids)
    pair_dists = D[np.triu_indices_from(D, k=1)]
    # Fast centroid-based separability proxy. This avoids the quadratic cost of
    # exact silhouette while still measuring whether samples lie closer to their
    # own class centroid than to the nearest competing centroid.
    dist_to_centroids = pairwise_distances(Xs, centroids)
    class_to_pos = {c: i for i, c in enumerate(keep_classes)}
    true_pos = np.array([class_to_pos[int(c)] for c in yk.values])
    d_true = dist_to_centroids[np.arange(len(Xs)), true_pos]
    dist_to_centroids[np.arange(len(Xs)), true_pos] = np.inf
    d_other = dist_to_centroids.min(axis=1)
    margin = (d_other - d_true) / (d_true + 1e-12)
    centroid_silhouette_proxy = np.mean((d_other - d_true) / (np.maximum(d_other, d_true) + 1e-12))
    return {
        "condition": condition,
        "n_windows": int(len(X)),
        "n_classes": int(len(keep_classes)),
        "centroid_silhouette_proxy": float(centroid_silhouette_proxy),
        "mean_inter_centroid_distance": float(np.mean(pair_dists)),
        "min_inter_centroid_distance": float(np.min(pair_dists)),
        "mean_nearest_centroid_margin": float(np.mean(margin)),
        "mean_feature_iqr_overlap": _feature_iqr_overlap(Xk, yk),
    }


def _plot_separability_projection(variants: Dict[str, Tuple[pd.DataFrame, pd.Series]], config: Config, paths: Dict[str, Path]) -> None:
    """Save PCA and t-SNE visualizations as qualitative separability diagnostics."""
    selected = list(variants.items())[:4]
    if not selected:
        return
    rng = np.random.default_rng(config.random_seed + 2001)
    # Random low-dimensional projection panels to keep the analysis fast
    # and independent of optional dimensionality-reduction packages.
    fig, axes = plt.subplots(1, len(selected), figsize=(4.2 * len(selected), 3.8), squeeze=False)
    for ax, (name, (X, y)) in zip(axes.ravel(), selected):
        n = min(900, len(X))
        idx = rng.choice(len(X), size=n, replace=False) if len(X) > n else np.arange(len(X))
        Xnum = X.select_dtypes(include=[np.number])
        Xs = StandardScaler().fit_transform(Xnum.iloc[idx].values.astype(float))
        Xs = np.nan_to_num(Xs, nan=0.0, posinf=0.0, neginf=0.0)
        try:
            rng_local = np.random.default_rng(config.random_seed + len(name))
            R = rng_local.normal(0, 1, size=(Xs.shape[1], 2))
            R = R / (np.linalg.norm(R, axis=0, keepdims=True) + 1e-12)
            Z = Xs @ R
            ax.scatter(Z[:, 0], Z[:, 1], c=y.iloc[idx].values, s=8, alpha=0.70)
        except Exception as e:
            ax.text(0.5, 0.5, f"Projection failed\n{e}", ha="center", va="center")
