def run_robustness_evaluation(supervised_results: Dict[str, Any], split: Dict[str, Any], config: Config, paths: Dict[str, Path]):
    """Evaluate best classifier under additive feature noise."""
    model = supervised_results["best_model"]
    X_train = pd.concat([split["X_train"], split["X_val"]], axis=0).reset_index(drop=True)
    X_test = split["X_test"].copy().reset_index(drop=True)
    y_test = split["y_test"].reset_index(drop=True)
    labels = sorted(CLASS_NAMES.keys())
    rng = np.random.default_rng(config.random_seed + 555)

    # Feature-wise scale from training distribution.
    scale = X_train.std(axis=0, numeric_only=True).replace(0, 1.0).values
    rows = []
    for noise_pct in [0.0, 0.02, 0.05, 0.10, 0.15]:
        X_noisy = X_test.copy()
        if noise_pct > 0:
            noise = rng.normal(0, noise_pct, size=X_noisy.shape) * scale
            X_noisy = pd.DataFrame(X_noisy.values + noise, columns=X_test.columns)
        pred = model.predict(X_noisy)
        metrics = summarize_metrics(y_test.values, pred, labels=labels)
        rows.append({"feature_noise_fraction_of_train_std": noise_pct, **metrics})

    robust_df = pd.DataFrame(rows)
    robust_df.to_csv(paths["tables"] / "table_06_robustness.csv", index=False)
    return robust_df


