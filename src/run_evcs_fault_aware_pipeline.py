def _pct_reduction(reactive_value: float, predictive_value: float) -> float:
    """Percentage reduction from reactive to predictive scenario."""
    reactive_value = float(reactive_value)
    predictive_value = float(predictive_value)
    if abs(reactive_value) <= 1e-12:
        return 0.0
    return 100.0 * (reactive_value - predictive_value) / reactive_value


def _extract_key_metrics(
    config: Config,
    supervised_results: Dict[str, Any],
    ablation_df: pd.DataFrame,
    anomaly_results: Dict[str, Any],
    maintenance_decisions: pd.DataFrame,
    op_results: Dict[str, pd.DataFrame],
    dataset_summary: pd.DataFrame,
    runtime_records: List[Dict[str, Any]],
) -> Dict[str, Any]:
    """Extract compact metrics for the repeated-seed stability table."""
    clf = supervised_results["classifier_metrics"].copy()
    best_row = clf.iloc[0]
    rf_rows = clf.loc[clf["model"] == "RandomForest"]
    rf_row = rf_rows.iloc[0] if len(rf_rows) else best_row

    full_ab_rows = ablation_df.loc[ablation_df["feature_set"] == "Full multi-source"]
    full_ab = full_ab_rows.iloc[0] if len(full_ab_rows) else ablation_df.iloc[-1]

    anom = anomaly_results["anomaly_metrics"].copy()
    oc_rows = anom.loc[anom["model"] == "OneClassSVM"]
    oc = oc_rows.iloc[0] if len(oc_rows) else anom.iloc[0]

    op = op_results["operational_impact"].copy()
    reactive = op.loc[op["policy"] == "Reactive maintenance"].iloc[0]
    predictive = op.loc[op["policy"] == "Predictive maintenance scenario"].iloc[0]

    events = op_results["event_detection"].copy()
    if len(events):
        event_detection_rate = float(events["detected"].mean())
        mean_delay_min = float(np.nanmean(events["detection_delay_h"].values) * 60.0)
        n_test_events = int(len(events))
    else:
        event_detection_rate = np.nan
        mean_delay_min = np.nan
        n_test_events = 0

    total_runtime = float(sum(r["runtime_s"] for r in runtime_records if r.get("stage") != "total_pipeline_runtime"))

    return {
        "seed": int(config.random_seed),
        "best_supervised_model": str(supervised_results["best_model_name"]),
        "best_supervised_accuracy": float(best_row["accuracy"]),
        "best_supervised_macro_f1": float(best_row["macro_f1"]),
        "rf_accuracy": float(rf_row["accuracy"]),
        "rf_macro_precision": float(rf_row["macro_precision"]),
        "rf_macro_recall": float(rf_row["macro_recall"]),
        "rf_macro_f1": float(rf_row["macro_f1"]),
        "full_multisource_ablation_accuracy": float(full_ab["accuracy"]),
        "full_multisource_ablation_macro_f1": float(full_ab["macro_f1"]),
        "oneclasssvm_roc_auc": float(oc["roc_auc"]),
        "oneclasssvm_pr_auc": float(oc["pr_auc"]),
        "oneclasssvm_f1_at_threshold": float(oc["f1_at_threshold"]),
        "oneclasssvm_false_alarm_rate": float(oc["false_alarm_rate"]),
        "oneclasssvm_unknown_class_recall": float(oc["unknown_class_recall"]),
        "n_labelled_windows": int(dataset_summary["windows"].sum()) if "windows" in dataset_summary.columns else int(dataset_summary.get("n_windows", pd.Series(dtype=float)).sum()),
        "n_test_events": n_test_events,
        "event_detection_rate": event_detection_rate,
        "mean_event_detection_delay_min": mean_delay_min,
        "reactive_port_availability": float(reactive["port_availability_proxy"]),
        "predictive_port_availability": float(predictive["port_availability_proxy"]),
        "availability_abs_improvement_pct_points": 100.0 * (float(predictive["port_availability_proxy"]) - float(reactive["port_availability_proxy"])),
        "reactive_unavailable_port_hours": float(reactive["unavailable_port_hours"]),
        "predictive_unavailable_port_hours": float(predictive["unavailable_port_hours"]),
        "unavailable_port_hours_reduction_pct": _pct_reduction(reactive["unavailable_port_hours"], predictive["unavailable_port_hours"]),
        "reactive_unmet_energy_kwh": float(reactive["unmet_energy_kwh"]),
        "predictive_unmet_energy_kwh": float(predictive["unmet_energy_kwh"]),
        "unmet_energy_reduction_pct": _pct_reduction(reactive["unmet_energy_kwh"], predictive["unmet_energy_kwh"]),
        "risk_triggered_action_windows": int(predictive["maintenance_actions_triggered"]),
        "pipeline_runtime_s": total_runtime,
    }


def run_pipeline_once(
    config: Config,
    paths: Dict[str, Path],
    *,
    generate_full_outputs: bool = True,
    verbose: bool = True,
) -> Dict[str, Any]:
    """Run one complete scenario-controlled EVCS experiment.

    When generate_full_outputs=False, the function still computes the metrics needed for
    repeated-seed stability analysis but skips the most manuscript-specific optional
    outputs such as explainability plots, robustness tables, and extra schematic figures.
    """
    runtime_records: List[Dict[str, Any]] = []

    def log(message: str) -> None:
        if verbose:
            print(message)

    log("[1/12] Saving configuration...")
    save_json(asdict(config), paths["base"] / "config.json")

    log("[2/12] Simulating healthy EVCS operation...")
    t0 = time.perf_counter()
    df_healthy = simulate_evcs_operation(config)
    runtime_records.append({"stage": "simulate_healthy_evcs", "runtime_s": time.perf_counter() - t0})

    log("[3/12] Injecting fault/anomaly scenarios...")
    t0 = time.perf_counter()
    df = inject_fault_scenarios(df_healthy, config)
    runtime_records.append({"stage": "inject_faults", "runtime_s": time.perf_counter() - t0})

    # Save raw data and events.
    df.to_csv(paths["data"] / "evcs_timeseries_with_faults.csv", index=True)
    events_df = df.attrs.get("events_df", pd.DataFrame())
    events_df.to_csv(paths["data"] / "fault_events.csv", index=False)

    log("[4/12] Creating window features...")
    t0 = time.perf_counter()
    X, y, meta = create_window_features(df, config)
    runtime_records.append({"stage": "create_window_features", "runtime_s": time.perf_counter() - t0})
    X.to_csv(paths["data"] / "window_features.csv", index=False)
    y.to_csv(paths["data"] / "window_labels.csv", index=False)
    meta.to_csv(paths["data"] / "window_metadata.csv", index=False)

    log("[5/12] Saving dataset summary...")
    t0 = time.perf_counter()
    dataset_summary = save_dataset_summary(df, X, y, meta, paths)
    runtime_records.append({"stage": "save_dataset_summary", "runtime_s": time.perf_counter() - t0})

    log("[6/12] Splitting train/validation/test by time...")
    split = split_by_time(X, y, meta, config)

    log("[7/12] Training and evaluating supervised classifiers...")
    t0 = time.perf_counter()
    supervised_results = train_classifiers(split, config, paths)
    runtime_records.append({"stage": "train_classifiers", "runtime_s": time.perf_counter() - t0})

    log("[8/12] Running feature-source ablation...")
    t0 = time.perf_counter()
    ablation_df = run_feature_ablation(split, config, paths)
    runtime_records.append({"stage": "feature_ablation", "runtime_s": time.perf_counter() - t0})

    log("[9/12] Running anomaly detection...")
    t0 = time.perf_counter()
    anomaly_results = run_anomaly_detection(split, config, paths)
    runtime_records.append({"stage": "anomaly_detection", "runtime_s": time.perf_counter() - t0})

    explainability = None
    if generate_full_outputs:
        log("[10/12] Computing explainability outputs...")
        t0 = time.perf_counter()
        explainability = compute_explainability(
            supervised_results["best_model"], split, supervised_results["best_predictions"], config, paths
        )
        runtime_records.append({"stage": "explainability", "runtime_s": time.perf_counter() - t0})
    else:
        runtime_records.append({"stage": "explainability", "runtime_s": 0.0})

    log("[11/12] Computing maintenance risk and decisions...")
    t0 = time.perf_counter()
    maintenance_decisions = compute_maintenance_risk(split, supervised_results, anomaly_results, config, paths)
    runtime_records.append({"stage": "maintenance_decision_layer", "runtime_s": time.perf_counter() - t0})

    log("[12/12] Evaluating operational impact" + (", robustness, and extra figures..." if generate_full_outputs else "..."))
    t0 = time.perf_counter()
    robustness_df = None
    if generate_full_outputs:
        robustness_df = run_robustness_evaluation(supervised_results, split, config, paths)
    op_results = evaluate_operational_impact(df, split, maintenance_decisions, config, paths)
    runtime_records.append({"stage": "robustness_and_operational_impact", "runtime_s": time.perf_counter() - t0})

    if generate_full_outputs:
        t0 = time.perf_counter()
        plot_system_architecture(paths)
        plot_fault_scenario_timeseries(df, paths)
        runtime_records.append({"stage": "plot_extra_figures", "runtime_s": time.perf_counter() - t0})
    else:
        runtime_records.append({"stage": "plot_extra_figures", "runtime_s": 0.0})

    total_runtime = sum(r["runtime_s"] for r in runtime_records)
    runtime_records.append({"stage": "total_pipeline_runtime", "runtime_s": total_runtime})
    save_runtime_report(runtime_records, paths)

    key_metrics = _extract_key_metrics(
        config,
        supervised_results,
        ablation_df,
        anomaly_results,
        maintenance_decisions,
        op_results,
        dataset_summary,
        runtime_records,
    )
    pd.DataFrame([key_metrics]).to_csv(paths["tables"] / "table_10_single_seed_key_metrics.csv", index=False)

    return {
        "df": df,
        "X": X,
        "y": y,
        "meta": meta,
        "split": split,
        "dataset_summary": dataset_summary,
        "supervised_results": supervised_results,
        "ablation_df": ablation_df,
        "anomaly_results": anomaly_results,
        "explainability": explainability,
        "maintenance_decisions": maintenance_decisions,
        "operational_results": op_results,
        "robustness_df": robustness_df,
        "runtime_records": runtime_records,
        "key_metrics": key_metrics,
    }



