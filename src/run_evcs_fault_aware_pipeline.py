def train_classifiers(split: Dict[str, Any], config: Config, paths: Dict[str, Path]):
    X_train, y_train = split["X_train"], split["y_train"]
    X_val, y_val = split["X_val"], split["y_val"]
    X_test, y_test = split["X_test"], split["y_test"]

    # Train on train+validation for final test, but use train/val if tuning is added later.
    X_fit = pd.concat([X_train, X_val], axis=0).reset_index(drop=True)
    y_fit = pd.concat([y_train, y_val], axis=0).reset_index(drop=True)

    labels = sorted(CLASS_NAMES.keys())
    rows = []
    fitted_models = {}

    for name, model in get_classifiers(config).items():
        t0 = time.perf_counter()
        model.fit(X_fit, y_fit)
        train_time = time.perf_counter() - t0

        t1 = time.perf_counter()
        pred = model.predict(X_test)
        infer_time = time.perf_counter() - t1
        infer_ms_per_window = 1000.0 * infer_time / max(len(X_test), 1)

        metrics = summarize_metrics(y_test.values, pred, labels=labels)
        row = {
            "model": name,
            **metrics,
            "train_time_s": train_time,
            "inference_ms_per_window": infer_ms_per_window,
        }
        rows.append(row)
        fitted_models[name] = model

    metrics_df = pd.DataFrame(rows).sort_values("macro_f1", ascending=False)
    best_name = metrics_df.iloc[0]["model"]
    best_model = fitted_models[best_name]

    # Save final model and metrics.
    dump(best_model, paths["models"] / "best_supervised_model.joblib")
    metrics_df.to_csv(paths["tables"] / "table_02_classifier_metrics.csv", index=False)

    # Per-class metrics for best model.
    best_pred = best_model.predict(X_test)
    report = classification_report(
        y_test, best_pred, labels=labels, target_names=[CLASS_NAMES[i] for i in labels],
        output_dict=True, zero_division=0,
    )
    per_class_rows = []
    for i in labels:
        d = report[CLASS_NAMES[i]]
        per_class_rows.append({
            "class_id": i,
            "class_name": CLASS_NAMES[i],
            "precision": d["precision"],
            "recall": d["recall"],
            "f1_score": d["f1-score"],
            "support": d["support"],
        })
    per_class_df = pd.DataFrame(per_class_rows)
    per_class_df.to_csv(paths["tables"] / "table_03_per_class_metrics.csv", index=False)

    # Confusion matrix figure.
    cm = confusion_matrix(y_test, best_pred, labels=labels)
    fig, ax = plt.subplots(figsize=(10, 8))
    disp = ConfusionMatrixDisplay(confusion_matrix=cm, display_labels=[CLASS_SHORT[i] for i in labels])
    disp.plot(ax=ax, values_format="d", xticks_rotation=35, colorbar=False)
    ax.set_title(f"Confusion Matrix - Best Model: {best_name}")
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_03_confusion_matrix.png", dpi=300)
    plt.close(fig)

    return {
        "best_model_name": best_name,
        "best_model": best_model,
        "classifier_metrics": metrics_df,
        "per_class_metrics": per_class_df,
        "best_predictions": best_pred,
    }


