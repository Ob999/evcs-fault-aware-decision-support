    events = df.loc[df["event_id"] >= 0, ["event_id", "label"]].drop_duplicates()
    if events.empty:
        return
    target_events = events.loc[events["label"] == 1]
    eid = int(target_events["event_id"].iloc[0]) if not target_events.empty else int(events["event_id"].iloc[0])
    event_rows = df.loc[df["event_id"] == eid]
    start = max(int(event_rows["step"].min()) - 36, 0)
    end = min(int(event_rows["step"].max()) + 36, len(df) - 1)
    sub = df.iloc[start:end].copy()

    # Plot selected normalized signals on one axis.
    signals = ["thd_i", "current_imbalance", "converter_temp_c", "ev_delivered_power_kw"]
    fig, ax = plt.subplots(figsize=(11, 5))
