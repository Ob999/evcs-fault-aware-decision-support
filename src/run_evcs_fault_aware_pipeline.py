def _is_valid_repeated_seed_run(results: Dict[str, Any]) -> Tuple[bool, str]:
    """Check that a repeated-seed run is comparable to the manuscript experiment.

    A run is considered valid for the repeated-seed stability table only if every
    operating/fault class appears in the held-out test windows. This avoids an
    artificial macro-F1 drop when a stochastic realization has no test windows for
    a minority class. The main single-seed manuscript run is unchanged.
    """
    y_test = results["split"]["y_test"]
    counts = y_test.value_counts().to_dict()
    missing = [CLASS_NAMES[i] for i in sorted(CLASS_NAMES.keys()) if counts.get(i, 0) == 0]
    if missing:
        return False, "missing test-window classes: " + ", ".join(missing)
    return True, "valid"


def run_repeated_seed_stability_analysis(config: Config, paths: Dict[str, Path]) -> Dict[str, pd.DataFrame]:
    """Run the evaluation over five valid stochastic seeds and summarize stability.

    The initial candidate seeds are taken from config.repeated_seed_values. If a
    candidate stochastic realization does not contain every class in the held-out
    test windows, it is logged and skipped, and the script continues to the next
    integer seed until five valid realizations are collected. This makes the
    repeated-seed macro-F1 values comparable across seeds.
    """
    target_valid_runs = len(list(config.repeated_seed_values))
    candidate_seeds = list(config.repeated_seed_values)
    next_seed = max(candidate_seeds) + 1 if candidate_seeds else config.random_seed

    print("\nRepeated-seed stability analysis")
    print("--------------------------------")
    print("Target valid runs:", target_valid_runs)
    print("Initial candidate seeds:", candidate_seeds)

    repeated_base = paths["base"] / "repeated_seed_runs"
    repeated_base.mkdir(parents=True, exist_ok=True)

    rows: List[Dict[str, Any]] = []
    accepted_seeds: List[int] = []
    skipped_rows: List[Dict[str, Any]] = []
    max_attempts = max(30, target_valid_runs * 10)
    attempts = 0

    while len(rows) < target_valid_runs and attempts < max_attempts:
        if attempts < len(candidate_seeds):
            seed = int(candidate_seeds[attempts])
        else:
            seed = int(next_seed)
            next_seed += 1
        attempts += 1

        seed_output_dir = repeated_base / f"seed_{seed:05d}"
        seed_config = Config(**{**asdict(config), "random_seed": seed, "output_dir": str(seed_output_dir)})
        seed_config.run_repeated_seed_stability = False
        seed_paths = make_output_dirs(seed_config.output_dir)
        print(f"\n[Repeated seed attempt {attempts}] seed={seed} -> {seed_output_dir}")

        results = run_pipeline_once(seed_config, seed_paths, generate_full_outputs=False, verbose=False)
        is_valid, reason = _is_valid_repeated_seed_run(results)
        key = results["key_metrics"]
        if not is_valid:
            print(f"  Skipped seed={seed}: {reason}")
            skipped_rows.append({"seed": seed, "reason": reason, **key})
            continue

        rows.append(key)
        accepted_seeds.append(seed)
        print(
            f"  Accepted seed={seed}: "
            f"RF macro-F1={key['rf_macro_f1']:.4f}, "
            f"OCSVM ROC-AUC={key['oneclasssvm_roc_auc']:.4f}, "
            f"unmet-energy reduction={key['unmet_energy_reduction_pct']:.2f}%"
        )

    if len(rows) < target_valid_runs:
        raise RuntimeError(
            f"Only {len(rows)} valid repeated-seed runs were found after {attempts} attempts. "
            "Increase max_attempts or relax the validation criterion."
        )

    per_seed_df = pd.DataFrame(rows)
    per_seed_df.to_csv(paths["tables"] / "table_10a_repeated_seed_metrics_per_seed.csv", index=False)

    skipped_df = pd.DataFrame(skipped_rows)
    if len(skipped_df):
        skipped_df.to_csv(paths["tables"] / "table_10c_repeated_seed_skipped_runs.csv", index=False)

    numeric_cols = [c for c in per_seed_df.columns if pd.api.types.is_numeric_dtype(per_seed_df[c]) and c != "seed"]
    summary_rows = []
    for col in numeric_cols:
        vals = per_seed_df[col].astype(float).values
        summary_rows.append({
            "metric": col,
            "mean": float(np.nanmean(vals)),
            "std": float(np.nanstd(vals, ddof=1)) if np.isfinite(vals).sum() > 1 else 0.0,
            "min": float(np.nanmin(vals)),
            "max": float(np.nanmax(vals)),
        })
    summary_df = pd.DataFrame(summary_rows)
    summary_df.to_csv(paths["tables"] / "table_10b_repeated_seed_metrics_summary.csv", index=False)
    _make_latex_repeated_seed_table(summary_df, paths["tables"] / "latex_table_repeated_seed_stability.tex")

    # Compact diagnostic stability figure.
    selected_labels = ["RF macro-F1", "OCSVM ROC-AUC", "OCSVM PR-AUC"]
    selected_metrics = ["rf_macro_f1", "oneclasssvm_roc_auc", "oneclasssvm_pr_auc"]
    means = [summary_df.loc[summary_df["metric"] == m, "mean"].iloc[0] for m in selected_metrics]
    stds = [summary_df.loc[summary_df["metric"] == m, "std"].iloc[0] for m in selected_metrics]

    fig, ax = plt.subplots(figsize=(8, 4.8))
    ax.errorbar(selected_labels, means, yerr=stds, fmt="o", capsize=4)
    ax.set_ylim(0, 1.05)
    ax.set_ylabel("Metric value (mean $\\pm$ standard deviation)")
    ax.set_title("Repeated-Seed Diagnostic Stability")
    fig.tight_layout()
    fig.savefig(paths["figures"] / "fig_09_repeated_seed_stability.png", dpi=300)
    plt.close(fig)

    print("\nRepeated-seed outputs saved:")
    print("  Accepted seeds:", accepted_seeds)
    if len(skipped_df):
        print("  Skipped seed report: outputs/tables/table_10c_repeated_seed_skipped_runs.csv")
    print("  outputs/tables/table_10a_repeated_seed_metrics_per_seed.csv")
    print("  outputs/tables/table_10b_repeated_seed_metrics_summary.csv")
